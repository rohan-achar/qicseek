<div class="img_caption_left">
<img src="../../../bin/img/photos/annualreport/2005-06/paulandjo.gif" alt="photo: paul and jo butterworth" width="274" height="241" /></div>
The Bren School received a $1.3 million gift from alumnus Paul Butterworth '74, MS '81 and his wife Jo.
<p>The gift will be applied to student scholarships, fellowships and other undergraduate and graduate student endeavor&rsquo;s at the Dean's discretion. </p>
<p>For his continued and generous support to the Bren School, as well as his professional achievements at pioneering new technologies and founding companies that have brought recognition to the School and the University. </p>
<p>Paul was recognized at UCI Alumni Association's 2006 Lauds and Laurels event with the Distinguished Alumnus Award.</p>
