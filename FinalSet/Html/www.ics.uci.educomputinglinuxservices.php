<div id="content_title">Unneeded Linux Services</div>

<blockquote>
<div id="content_text">

<p>The more services running on your machine, the greater the chance
that one or more will have a known security hole.&#160; Therefore, you should
turn off any unneeded services and ensure any services you run are the 
latest
version and are properly configured.
          </p>
<ul>
<li> Services in <code>/etc/inetd.conf</code>
<ul>
<li> <code>Inetd</code> is the daemon that manages many 
system
                services.&#160; These are listed in its configuration file, <code>/etc/inetd.conf</code>,
                which can easily be modified to prevent <code>inetd</code>
 from
                starting unwanted services.&#160; If you are the only user of
                your workstation, you can have almost everything 
commented out and will most likely not notice their absence.</li>
<li> Services which are often unneeded include: <code>echo</code>,
 <code>daytime</code>, <code>chargen</code>, <code>time</code>, <code>login</code>,
 <code>shell</code>,
                and <code>exec</code>.&#160; To turn these off, follow the 
instructions
                at <a href="http://www.oit.uci.edu/support/security/turning-off-inetd-service.html">
 Turning
                off an <i>inetd</i>-launched service</a>.</li>
<li> Services you probably want to keep include: <code>telnet</code>,
 <code>ftp</code>, <code>identd</code>.</li>
</ul>
</li>
<li> Services started from system start-up scripts</li>
<p> Many other services are started in system start-up 
scripts. You
              should review your start-up scripts and comment out ones 
which
              are not appropriate for your workstation's purpose. For 
example, <code>rpc.mountd</code> and <code>rpc.nfsd</code> are
              probably unneeded on a single user workstation. <i>kill -9</i>
 any
              unwanted services and then comment them out in the 
appropriate
              system start-up file.            </p>
<li> Other services
<ul>
<li> To determine what other services you are running, use
 the
                command:
<pre>          netstat -a | more</pre>
</li>
<li> Use your package manager to list the packages 
installed (<code>dpkg</code> for
                Debian and <code>rpm</code> for RedHat).</li>
<li> Unless your machine is acting as a mail server, you 
should <a href="http://www.oit.uci.edu/support/dcs/security/turning-off-sendmail.html">
 turn
                  off sendmail</a>  since it is very large and is one of
 the
                  most common targets for attacks because of its 
numerous security
                  holes. Note: <code>sendmail</code> is not necessary if
 you
                  are using <code>pop</code> to read your mail. </li>
<li> If you are not actively sharing files or printers from
 your
                machine with machines running a Microsoft operating 
system, you
                should <a href="http://www.oit.uci.edu/support/dcs/security/turning-off-samba.html">
 turn
                off samba</a>.</li>
</ul>
</li>
<li> Do not run NIS or NIS+ if you do not need them. A 
standalone machine
              will never need these.
<ul>
<li> Define each netgroup to contain only usernames or 
only hostnames.&#160; All
                utilities parse <code>/etc/netgroup</code> for either 
hosts or
                usernames, but never both.&#160; Using separate netgroups 
makes
                it easier to remember the function of each netgroup. The
 added
                time required to administer these extra netgroups is a 
small
                cost in ensuring that strange permission combinations 
will not leave your machine in an insecure state.</li>
</ul>
</li>
<li> Web servers: If you are running a web server, make sure
 you
              have the latest available release.&#160; The latest version of
              Apache can be obtained from the official <a href="http://www.apache.org/dist/">Apache
              Distribution</a> site.</li>
<li> Anonymous FTP
<ul>
<li> Make sure that you are running the most recent 
version of <code>ftpd</code>.
                There are several well-known security problems with the 
ones
                that ship on distribution media.</li>
<li> Check your anonymous FTP configuration. It is 
important to
                follow the instructions provided with the operating 
system to
                properly configure the files and directories available 
through
                anonymous FTP (for example, file and directory 
permissions, ownership
                and group).</li>
<li> You should not use your system's standard password 
file or
                group file as the password file or group file for FTP.</li>
<li> The anonymous FTP root directory and its two 
subdirectories, <code>etc</code> and <code>bin</code>,
                should not be owned by the FTP user.</li>
</ul>
</li>
</ul>


</div>
