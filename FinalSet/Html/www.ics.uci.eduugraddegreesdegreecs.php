<p>Computer science is the catalyst for every evolutionary &#8211; and revolutionary &#8211; step in computer development. 
              </p>
<p>From mathematical theories, data structures and algorithms to the operating systems and programs that employ them, an understanding of computer science is essential if you wish to develop the next advances in computer technology and applications.</p>
<p>The Computer Science program at UC Irvine is internationally recognized for its unique group of faculty and researchers, outstanding students and cutting edge educational programs. </p>
