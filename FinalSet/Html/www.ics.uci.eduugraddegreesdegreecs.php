<h4>What can I do with this degree?  </h4>
<p><img alt="photo:: computer science student" class="left" height="133" src="../../bin/img/photos/degrees/cs_do.jpg" width="152"/>Graduates of the Computer Science program will be in a position to pursue a variety of careers that involve the design and development of embedded systems, programming languages, compilers, networks and operating systems. </p>
<p>They can be principal designers or involved in implementation, typically at companies that design, implement and sell these products. They may find themselves in charge of large-scale deployments and/or customizations at the organizations that use them. </p>
<p>Finally, the strong scientific preparation allows students to become involved in such areas as artificial intelligence and computational biology &#8211; whether in graduate school or industry.</p>
<p>Many students also go on to graduate school, continuing their studies, conducting research, and earning graduate degrees in software engineering, computer science, information science, management and law. </p>
