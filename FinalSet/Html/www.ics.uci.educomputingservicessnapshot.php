<blockquote>
<div id="content_text">

<p>Snapshot is only available for accounts on the TRON server. &#160;If you cannot find a .snapshot folder in your home directory (see below for steps), please contact helpdesk to do a&#160;<a href="restore.php">file restore</a>.</p>
<p><strong>** Snapshots are taken daily for only up to 30 days </strong><strong>**<br/></strong></p>
<p><b>Windows</b></p>
<p>Your home directory (generally H:) drive is automatically mapped if you are logged into a machine on the UCI-ICS domain. &#160;If you need to map your home directory, please refer to <a href="../account/mapdrive_win.php">Mapping Home/Group Directory</a>.</p>
<ol>
<li>Navigate to where your deleted/corrupted file is on the H: drive (or whichever letter you assigned to your ICS mapped drive).
<ul>
<li>If your file is corrupted, you will need to rename it with a .old extension (e.g. filename.old).</li>
</ul>
</li>
<li>Open another window and navigate to the .snapshot directory.
<ul>
<li>From the <b>Start</b> menu, go to <b>Run</b> and type in <i><b>H:\&#126;snapshot</b></i> and hit <b>OK</b>.</li>
<li>You should now see folders named in&#160;<strong>yyyy-mm-dd_time.daily</strong> format. &#160;Find the most recent nightly folder where you file is intact.</li>
<li>Continue until you find the file that you want to restore.</li>
</ul>
</li>
<li>Copy and paste the file from <b>Step 2</b> into the window from <b>Step 1</b>.</li>
</ol><b>Unix/Linux</b><br/><ol>
<li>Login to one of the <a href="../linux/hosts.php">ICS hosts</a>.</li>
<li>From the root of your home directory, cd .snapshot (you will not see the folder listed, but you will be able to cd into it).<br/>
<ul>
<li>If you have a corrupted file, rename it first (e.g. filename.old).</li>
</ul>
</li>
<li>You should now see folders named in <strong>yyyy-mm-dd_time.daily</strong> format. &#160;Find the most recent daily folder where you 
file is intact.</li>
<li>Continue until you find the file that you want to restore.</li>
<li>Copy the file over to the original location by using:
<ul>
<li>user@ics &#126;/.snapshot/2010-12-01_0600-0700.daily <br/>$ cp backupfile &#126;/original/folder/backupfile</li>
</ul>
</li>
</ol>


</div>
