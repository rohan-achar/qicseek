<div id="content_title">Creating Personal Webpage</div>

<blockquote>
<div id="content_text">

<p><i>This document is a modified version of  the
                one written for UIUC by Ed Kubaitis.
            Send comments or questions to <a href="mailto:webmaster@ics.uci.edu"> webmaster@ics.uci.edu</a>. </i> </p>
<h3>What is a Personal Web?</h3>
A personal webpage is a section of webspace in which you (as opposed to
          the webmaster) have control over the contents. &#160;Our server allows a special subdirectory of your UNIX home directory
          to act as an attachment to the server's document namespace (the part
          of the URL which
follows <code><b>http://www.ics.uci.edu/</b></code>). &#160;Since this area is in your
home directory, it get counted as part of your quota. &#160;This is where your
homepage resides.
<h3>Learn about HTML and URLs</h3>
HTML is the <i>HyperText Markup Language</i> used for documents on the World
Wide Web. &#160;A URL is a <i>Uniform Resource Locator</i>, the WWW address of a document
or other network resources. &#160;Although other document types (e.g. PostScript) can
be accessed through the Web, HTML is by far the best format for documents which
are intended to be read online. &#160;There are many good tutorials available online if you would like to find out more information about this topic.
<h3>  </h3>
<h3>Setting Up Your &#126;/public_html Directory</h3>
<p>There are two ways to setup your publich_html directory.</p>
<ol>
<li>Create the directory manually, then set the proper permissions.
<ul>
<li>Using your ICS account, login to one of the <a href="../linux/hosts.php">ICS servers</a>&#160;via PuTTY or your preferred SSH client.&#160;</li>
<li>Once you sre logged in, make your home directory world searchable (711) or world 
readable 
            (755). &#160;Use the <code>chmod</code> command to change file 
permissions. 
          
<ul>
<li>
<p><code>chmod 711 /home/you_username</code></p>
</li>
<li>run the command <code>ls -la</code> to check that you have set it up correctly. &#160;The first line of the output should disply something like:
<ul>
<li>
<code>drwx--x--x &#160; 62 your_username ugrad 18K Oct 16 10:00 ./</code>
</li>
</ul>
</li>
</ul>
</li>
<li> Create a subdirectory <code>public_html</code> in your home 
directory. &#160;This directory must also be at least world readable (755).
<ul>
<li><code>mkdir public_html</code></li>
<li><code>chmod 755 public_html</code></li>
</ul>
</li>
<li>Place the documents you want to serve in this directory or in further subdirectories. &#160;You can simply go to your home directory (H-drive on lab machines) to copy/paste files/folders into the public_html folder. &#160;For more instructions on how to map to your home directory, please see instructions for <a href="../account/mapdrive_win.php">Windows</a>&#160;or <a href="../account/mapdrive_mac.php">Mac</a>.</li>
<li>All documents under <code>&#126;/public_html</code> should be world readable (644) and
all subdirectories should be world readable (755). &#160;Do not leave any auxiliary,
temp, scratch, backup, or
otherwise undesirable files under <code>&#126;/public_html</code>.
</li>
</ul>
</li>
<li>Run a script and answer the prompted questions.
<ul>
<li>Using your ICS account, log in&#160;to one of the <a href="../linux/hosts.php">ICS servers</a>.</li>
<li>Run the command <code>/pkg/ics/bin/make_my_web</code></li>
</ul>
</li>
</ol>
<p> All filenames must end with an appropriate extension (the part after the
  last dot). &#160;For example, all HTML documents should have filenames ending in <code>.html</code>,
  all text documents should have filenames ending in <code>.txt</code>, and all
  PostScript documents should have filenames ending in <code>.ps</code>. &#160;Compressed documents
  should have filenames ending in <code>.extension.Z</code> or <code>.extension.gz</code> (for <code>compress</code> or <code>gzip
  -9</code>, respectively).</p>
<p> Also, please avoid using file/pathnames which are excessively long. &#160;Long
  filenames make for long URLs which are difficult for users to type and problematic
  for many WWW clients.</p>
<p> The URL for files in this directory is http://www.ics.uci.edu/&#126;username/filename</p>
<p>For example, for login name <code>peteranteater</code>, the URL is http://www.ics.uci.edu/&#126;peteranteater/myfile.html and will access &#126;peteranteater/public_html/myfile.html</p>
<p> The URL <code>http://www.ics.uci.edu/&#126;peteranteater/ </code> (i.e. no filename)
  is the same as http://www.ics.uci.edu/&#126;peteranteater/index.html</p>
<p>So if you save your personal home page as the file <code>&#126;/public_html/index.html</code>,
people can access it with the short URL. &#160;You should always include the trailing
slash in URLs when referring to a directory --some servers can get confused
otherwise. &#160;If there is no file called <code>index.html</code>, the short URL
will generate a menu of all files if the directory is world readable (755) or
an error message if it is only world searchable (711).</p>
<p> For security reasons, <b>never</b> put symbolic links under <code>&#126;/public_html</code> which
  point to directories outside of <code>&#126;/public_html</code>. &#160;Symbolic links
  to other files are okay provided that both the link and its destination are
  owned by the same user. &#160;Symbolic links from outside directories pointing to
  destinations under <code>&#126;/public_html</code> are always okay.</p>
<h3>Other Tips and Notes</h3>
You can test if things are working the way you want by checking them with your
favorite browser. &#160;For example, using Firefox, you can use the <strong>File, Open file  </strong>menu item or by copy pasting your website's URL into your browser's address bar. <br/>
<p> You can also inspect the HTML used for any document you find on the Web using
  your favorite browser. &#160;With Firefox, use the <strong>View, Page</strong> source menu item to look at a page's source code.&#160;</p>
<p> Always remember that the whole world is watching what you do. &#160;Do not put anything in your personal webspace which is not intended to be public 
          information. &#160;Furthermore, you must always obey the <a href="../policy/index.php">Ethical 
          Use of Computing Resources</a> guidelines to which you agreed before 
          receiving an ICS account. &#160;Please review those guidelines before making 
          any questionable material available to the World Wide Web. &#160;If you have any questions, feel free to email <a href="mailto:helpdesk@ics.uci.edu">helpdesk</a>.</p>


