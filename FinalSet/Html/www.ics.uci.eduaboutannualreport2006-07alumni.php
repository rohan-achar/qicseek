<p><a href="http://www.ics.uci.edu/community/alumni/mentor">ICS ALUMNI MENTOR PROGRAM</a> <br />
  Each Fall quarter, ICS offers its alumni the opportunity to get involved with current <br />
  students through the ICS Mentor Program. Students are able to select mentors <br />
  from a diverse pool of alumni. Mentors and students are matched for the duration <br />
  of the academic year, with the hope that both alumni and students will be able to <br />
  form long-lasting relationships.  </p>
<p><strong>CAREER EVENTS</strong> <br />
  Alumni are always welcomed back to campus for Career Events that range from <br />
  the UCI Career Center&rsquo;s annual Career Fest panels, to conducting workshops with <br />
  one of the several student groups at ICS. If you are interested in this type of <br />
  program, please email <a href="mailto:alumni@ics.uci.edu">alumni@ics.uci.edu</a>. </p>
<p><strong><a href="http://www.alumni.uci.edu">UCI ALUMNI ASSOCIATION</a></strong> <br />
  The UCI Alumni Association strives to enrich the lives of UCI alumni and engage <br />
  them in a lifelong advancement of the university. Members make a difference <br />
  in the tone and vitality of the organization. Founded in 1968, the association <br />
  seeks to provide alumni with a lasting bond to the university. Not a member yet? <br />
  Information on membership, benefits and alumni events can be found online.</p>
