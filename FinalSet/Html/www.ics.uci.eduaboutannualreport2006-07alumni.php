<p><strong>OPPORTUNITIES FOR INVOLVEMENT</strong> <br />
  There are many opportunities for involvement with ICS, both for those who like <br />
  to commit to long-term program, and those who have limited time and prefer to <br />
  commit to programs that are easier to fit into their busy every-day schedule.</p>
