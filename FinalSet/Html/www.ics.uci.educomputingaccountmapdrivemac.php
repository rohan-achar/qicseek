<div id="content_title">Mapping Home/Group Directory - Mac</div>

<blockquote>
<div id="content_text">

<span style="color: #990000;">
<p style="text-align: center;"><strong>*** Important ***</strong></p>
<p style="text-align: center;"><strong><em>If you are using the UCInet Mobile Access (UCI wireless) or connecting from off-campus <strong><em>(including on-campus housing)</em></strong>,</em></strong></p>
<p style="text-align: center;"><strong><em> please make sure that you are connected via the <a href="http://www.oit.uci.edu/security/vpn/vpninstall.html" title="VPN">UCI VPN</a> first.</em></strong></p>
</span>
<p>&#160;</p>
<ul>
<li><a href="#quick">Quick instructions</a></li>
<li><a href="#pictures">Instructions with pictures</a></li>
<li><a href="#errors">Common&#160;errors</a></li>
</ul>
<p>&#160;</p>
<hr/>
<p><a name="quick"></a>Quick Instructions</p>
<ol>
<li>From the <b>Finder</b>, click <b>Go</b>, 
            then select <b>Connect to Server</b>.</li>
<li>In the&#160;<b>Server Address</b>, enter one of the following and click <strong>Connect</strong>.
<ul>
<li>Home directory:&#160;<code>smb://tron.ics.uci.edu/your_username</code>
<ul>
<li>If you are running Mavericks, please enter: <code>cifs://tron.ics.uci.edu/your_username</code></li>
</ul>
</li>
<li>Group directory:&#160;<code>smb://samba.ics.uci.edu/group_acct_name</code></li>
</ul>
</li>
<li>For&#160;<strong>Name</strong>, enter&#160;<strong>uci-ics\yourusername</strong>&#160;then enter your ICS password and click&#160;<b>Connect</b>.</li>
<li>Your account is now mapped.</li>
<li>If you would like to save the mapped drive as a shortcut, drag the newly mapped drive over to&#160;<b>DEVICES</b>&#160;on the left.</li>
<li>When you map the drive next time, it will automatically show up under&#160;<b>DEVICES</b>.</li>
</ol>
<ol>
</ol>
<p>&#160;</p>
<hr/>
<p><a name="pictures"></a>Instructions with pictures</p>
<ol>
<li>From the&#160;<b>Finder</b>, click on&#160;<b>Go</b>, then select&#160;<b>Connect to Server</b>.<br/>
<p><img alt="Connect to server" src="../bin/img/account/mapdrive_mac/map1.jpg"/></p>
</li>
<li>In the <b>Server Address</b>, enter&#160;one of the following and click&#160;<strong>Connect</strong>.
<ul>
<li>Home directory:&#160;<code>smb://tron.ics.uci.edu/your_username</code>
<ul>
<li>If you are running Mavericks, please enter: <code>cifs://tron.ics.uci.edu/your_username</code></li>
</ul>
</li>
<li>Group directory: <code>smb://samba.ics.uci.edu/group_acct_name</code></li>
</ul>
<p><img alt="server address" height="254" src="../bin/img/account/mapdrive_mac/map2.jpg" width="490"/></p>
</li>
<li>For <strong>Name</strong>, enter <strong>uci-ics\yourusername</strong> then enter your ICS password and click <b>Connect</b>.
<p><img alt="Enter username and password" height="294" src="../bin/img/account/mapdrive_mac/map3.jpg" width="431"/></p>
</li>
<li>Your account is now mapped.
<p><img alt="mapped" src="../bin/img/account/mapdrive_mac/map4.jpg"/></p>
</li>
