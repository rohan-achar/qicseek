<div id="content_title">Della Halim spotlight</div>

<blockquote>
<div id="content_text">

<p><strong>A Helping Hand </strong>
</p>
<div class="img_caption_left">
<img alt="photo:: della halim" height="192" src="../../../bin/img/photos/people/della_halim.jpg" width="220"/>
<p>
Della Halim
</p>
</div>
<p>Della Halim faced a dilemma when she came to UCI.</p>
<p>The Arcadia native didn&#8217;t know a lot of people on campus but she didn&#8217;t want to drive back and forth to Los Angeles all the time to hang out with her friends from high school. </p>
<p>So in order to get to know more people, she decided to get involved with <a href="http://www.ics.uci.edu/&#126;wics/">UCI&#8217;s  Women in Information and Computer Sciences (WICS)</a> student group. </p>
<p>&#8220;Becoming involved with WICS meant having more friends at UCI and becoming more engaged with school,&#8221; Halim said. &#8220;I wanted to network, learn, have fun, and experience the college years.  I felt that it was the right opportunity for me.&#8221;</p>
<p>In her fourth year of involvement with WICS, Halim has become the group&#8217;s president and is expanding on and establishing WICS&#8217; initiatives to help and encourage women to pursue a college degree and a successful career in the computer science fields. </p>
<p>&#8220;With only about 13 percent of students in ICS being female, a freshman or sophomore woman may feel out of place and/or intimidated,&#8221; Halim said. &#8220;We are here to let them know that they are not alone and that we have been through what they are going through.&#8221;</p>
<p><strong>PROVIDING ASSISTANCE </strong></p>
<p>To assist female computer science students feel more at home in the often male dominated classes, WICS has a mentoring program which pairs up a freshman, sophomore, or new transfer student with a junior, senior, or graduate student.</p>
<p>&#8220;The program aims to help the mentee gain confidence in their major and help the mentor find satisfaction in self-reflection and giving back to the community,&#8221; Halim said. &#8220;Furthermore, it provides both the mentee and the mentor gain personal support from someone who can closely relate to their experiences.&#8221;</p>
<p>The group also organizes at least 8 events per quarter that run the gamut from recruiting and social events to workshops on how to utilize the Eclipse software tool for programming purposes. </p>
<p>During winter quarter the group held a Mock Technical Interview event with the Career Center and several companies such as Northrop Grumman, Unisys, Google, Sony, and Relsys.</p>
<div class="highlight_box_left">
<a href="index.php">Check out our spotlight page</a> to read more profiles of Bren School students, faculty and alumni.
</div>
<p>&#8220;We were able to better prepare students for a technical interview by allowing them to have a one-on-one 30-minute interview with the company of their choice,&#8221; Halim said. &#8220;We had over 40 interviews and 7 resume critiques available. The event was a great success on both the students and company sides.&#8221; </p>
<p>Besides connecting students with companies and providing technical workshops, WICS encourages students to get to know their professors better through events such as the Faculty Breakfast and Dinner with the Dean and Female Professors. </p>
<p>&#8220;Feeling involved in the school encourages students to stay and do better with their academic goals,&#8221; Halim said. </p>
<p>WICS also encourages members to get involved with the WICS board by becoming part of a committee and running for a board position. These positions provide students with the experience of working in a team as well as being a leader, something that may not always be taught in classes. </p>
<p>&#8220;I am proud to have the WICS board always work together as a team even when life takes over with unexpected challenges,&#8221; Halim said. &#8220;When other organizations may have fallen apart because of the added pressure and challenges, we stayed together and triumphed.&#8221;</p>
<p><strong>REACHING OUT TO THE COMMUNITY </strong></p>
<p>But the WICS mission isn&#8217;t confined solely to the campus, the group also gives back to the larger Orange County community by holding outreach events that encourage young girls to pursue a higher education in computer science. </p>
<p>One example is the WICS Elementary School Outreach Program, which was started in 2006 and received the Most Outstanding New Program award from the Dean of Students. </p>
<p>The program enabled elementary school students from Girls Inc. to visit UCI and find out what computer science is and learn about some of the interesting research that goes on. </p>
<p>&#8220;Not only does this benefit the young girls, but it also reminds our members why computer science is fun and beneficial,&#8221; Halim said. &#8220;We help women succeed by providing them various opportunities, networks, and personal support.&#8221;</p>
<p><strong>SOLVING PUZZLES </strong></p>
<p>WICS outreach efforts will probably inspire some of the girls to follow in Halim&#8217;s footsteps as a student in the Bren School, but for Halim, it was her love of puzzles that drew her to computer science. </p>
<p>In high school she learned C++ and was intrigued by the concept of writing her own programs. </p>
<p>&#8220;I often thought of the projects that we had to do in C++ as puzzles,&#8221; Halim said. &#8220;For me, programming was equivalent to solving puzzles, and I like solving puzzles.&#8221;</p>
<p>She created her own programs so she won&#8217;t have to do any calculations by hand to solve her math problems.</p>
<p>&#8220;My programs would ask for the values of the variables, do the calculation, and simply provide me the answer I was looking for,&#8221; Halim said. &#8220;Even if figuring out how to write the code took longer than solving the problem by hand, I felt that it was worth coding if it meant that I could use the program and save time later on in the future.&#8221; </p>
<p>She choose the Bren School to continue her computer science education because of the recommendation she got from her aunt and uncle who are ICS alumni.</p>
<p>The recommendation was spot on. </p>
<p>&#8220;The Bren School of ICS, has prepared me for life after college in numerous ways,&#8221; Halim said. &#8220;I have received valuable education regarding information and computer sciences that I believe would not have been achievable had I not chose to attend UCI.&#8221;</p>
<p>The school has also offered Halim the opportunity to work on challenging real world problems through her participation with Team XAR, the Bren School&#8217;s autonomous car entry for the 2005 DARPA Grand Challenge. </p>
<p>Working as a member of professor Ian Harris&#8217; Vision Evaluation: Sensor Fusion &amp; Obstacle Detection group she worked to assist the car&#8217;s LIDAR (Light Detection and Ranging) system in detecting sparse objects by finding software/hardware that would enhance it by finding alternate sensors when it becomes unreliable. </p>
<p>&#8220;We had to be able to differentiate between various obstacles and determine whether it is safe to drive through them (eg. boulders vs. bushes) as well as anomalies as such barbed wires,&#8221; Halim said. </p>
<p>The experience gave Halim a first hand look at the research process such as experimentation, writing papers about the group&#8217;s findings and making presentations regarding the progress and conclusions.</p>
<p>With graduation around the corner she is keeping her post-graduate options open. </p>
<p>&#8220;Regardless of which path I decide to take, I want to continue to work on furthering the science and technology education of younger children,&#8221; Halim said, &#8220;I believe that companies, organizations, and colleges should contribute to ensuring that children value education and develop a passion for learning.&#8221;<br/>
                <br/>
              But until then, Halim will continue to stay involved and cherish her remaining time at UCI.</p>
<p><i>- Eric Kowalik</i></p>


