<blockquote>
<div id="content_text">

<p>Please login into <a href="https://mailboss.ics.uci.edu">Mailboss</a>&#160;using your ICS account information. </p>
<hr/>
<p><b>Spam Filter</b></p>
<p>If you choose to, you can have Mailboss run a spam filter on your mail. Your email client can also do this. When scanned, each email message is assigned a spam score. The higher the score, the more likely that it is spam. You can then choose what to do depending on the level of the score.</p>
<p><b>Vacation</b></p>
<p>When going on a vacation, you can set an auto response. An end date can also be entered so that it will disable on the day you choose without having to log back in. Only one message will be sent per email address. So if a person sends you multiple emails, he/she will only receive one auto-response email.</p>
<p><b>Quota Check</b></p>
<p>You can have the system send you a reminder when you are close to hitting the limit on your quota. Please specify the minimum amount of free space left before you get a notice.</p>


</div>
