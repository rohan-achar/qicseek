<div id="content_title">Sophos Anti-Virus on Windows</div>

<blockquote>
<div id="content_text">

<p>* Before installing Sophos, please make sure that you do not have any other anti-virus programs already installed.</p>
<p>** If you are 
connecting from off campus or if you are on the UCI wireless 
network, please make sure that you are connected via the <a href="http://www.oit.uci.edu/security/vpn.html">UCI VPN</a> first 
before proceeding.</p>
<ol>
<li>Right-click on <b>My Computer</b> and select <b>Map
            Network Drive</b>.</li>
<li>Select any drive letter for the drive and enter the following path for the <b>Folder</b>:
<p><code>\\sophos-server.ics.uci.edu\sophos</code></p>
</li>
<li>Click on <strong>Connect using a different user name</strong> and enter your ICS
            username and password.<br/>
            
<ul>
<li>Username: &lt;<b>your_username</b>&gt;@ics.uci.edu</li>
<li>Password: <b>ICS password</b></li>
</ul>
</li>
<li>Double click on <b>setup.exe</b>.
<ul>
<li>Note: You can also copy the entire content of the 
folder on
                to another folder on your computer and then run <b>setup.exe</b>.</li>
</ul>
</li>
<li>Continue through the setup by clicking on <b>Next</b> until you
            get to <b>Update Source</b>. For the&#160;<b>Address</b>, enter:
            <br/><br/><code>\\sophos-server.ics.uci.edu\SophosUpdate\CIDs\S000\SAVSCFXP\</code><br/><br/>
            and enter your ICS username and password.
<p><img alt="Sophos - update source" src="../bin/img/services/sophos_win/update_source.jpg"/></p>
</li>
<li>Proceed through the remaining setup windows to begin the install. &#160;When the process has completed, you should now see a blue shield
            icon in your system tray. &#160;Right click on it and select <b>Open
 Sophos Endpoint Security and Control</b>.<b>
<p><img alt="Sophos - system tray" src="../bin/img/services/sophos_win/sys_tray.jpg"/></p>
</b></li>
<li>Click on <b>Configure updating</b> and verify the information that you previously entered during setup. </li>
<li>Go under the <b>Schedule</b> tab and modify the time interval between
            updates to your liking. &#160;This will enable automatic updating 
but will
            only work if you are on campus or connected to the VPN when 
the program
            tries to update itself.</li>
<li>Right click on the blue shield
            icon again and click on <b>Update Now</b>.<br/>
            
<ul>
<li>If you get an error "Could not contact server" and
                you are off campus, make sure you are connected to the 
VPN. If
                you are, then right click on the blue shield
                icon in the system tray and select <b>Open Sophos 
Endpoint Security and Control</b>
                and click on <b>Configure updating</b>.</li>
<li>Click on <b>Primary Server</b> and change <b>sophos-server.ics.uci.edu</b>
                to its IP address: <b>128.195.1.249</b></li>
<li>Select <b>Update Now</b> again. </li>
</ul>
</li>
<li>Updates usually need to be performed manually. &#160;It is recommended
            that you do this weekly to obtain the latest virus 
definitions. &#160;Sophos
            has a new version released each month so, once a month, the 
update
            will take longer as a new version needs to be downloaded and
 installed. <br/><i>*** If you are 
connecting from off campus or if you are on the UCI wireless 
network, you will need to be connected via the VPN in order to do the update.</i></li>
</ol>


</div>
