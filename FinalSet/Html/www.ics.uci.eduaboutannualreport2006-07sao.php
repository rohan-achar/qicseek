<h4>HIGHLIGHTS FOR 2006-07</h4>
</p>
<br />
<p>Student Affairs hosted many programs and events that addressed the varying needs of students <br />
  at different points in their educational progress.  </p>
<p>From programs for newly admitted students to those who are about to graduate, the aim is to provide information, facilitate networking and
  build community.  The SAO staff also provided administrative support to the Associate Dean for 
  Student Affairs in curriculum review and development. </p>
