<p><h4>ABOUT STUDENT AFFAIRS</h4></p>
<br />
<img src="/bin/img/photos/annualreport/2006-07/photo_sao.jpg" alt="photo: professor ian harris at the pizza social" width="670" height="154" border="1"><br />
<div id="caption">Vice Chair - Graduate Studies,  Ian Harris (fifth from left) at the new student welcome pizza social.</div>
<p>The Student Affairs Office (SAO) is staffed by nine professionals and several peer advisors <br />
who are committed to the success of our students, guiding them through important 
milestones and encouraging their timely progress to graduation.</p>
<p>In a typical year, SAO has approximately 3,600 advising contacts with undergraduates and 1,200 with graduate students.</p>
<p> Additionally, SAO hosts numerous special events for prospective and current students, fosters <br />
  student leadership and campus involvement, and encourages community among School 
  faculty and student scholars. </p>
<p>These efforts contribute to the School&rsquo;s goals of attracting, retaining and graduating students whose accomplishments while at UCI &ndash; and later &ndash; reflect 
  the quality and scope of their Bren School of ICS education.</p>
