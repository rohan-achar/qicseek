<title>Understanding and Setting UNIX File Permissions</title>
<h1>Understanding and Setting UNIX File Permissions</h1>
<h2>1. Introduction</h2>
<p>This guide gives a brief introduction to UNIX file permissions, provides
      instructions on how to perform simple configuration of UNIX file
      permissions, and shows how to find files that may have more permissive
      settings than intended.</p>
<h2>2. Theory of Operation</h2>
<p>UNIX assigns file permissions to three broad categories:</p>
<ol>
<li>The user that owns the file</li>
<li>The group that owns the file</li>
<li>Everyone else (a.k.a 'world' or 'other')</li>
</ol>
<p>Each permission category can be assigned three permissions:</p>
<ol>
<li>(r)ead</li>
<li>(w)rite</li>
<li>e(x)ecute</li>
</ol>
<p>UNIX permissions can be represented as symbols and as octal digits. Fig.
      1 shows a table of possible values:</p>
