<span style="font-style: italic;">Fig. 1: Table of Permission Combinations</span><br/>
<p>The effect of these permissions varies by file type. There are numerous
      file types, but this guide will only deal with the following types:</p>
<ol>
<li><span style="font-family: monospace;">'-'</span> &#8212; Regular file</li>
<li><span style="font-family: monospace;">'d'</span> &#8212; Directory</li>
</ol>
<p>When applied to a regular file, UNIX permissions have the following
      effect:</p>
<ol>
<li>Read permission allows the file to be opened</li>
<li>Write permission allows the file to be modified</li>
<li>Execute permission allows the file to be executed directly from the
        shell, if it is a script or binary file</li>
</ol>
<p>When applied to a directory, UNIX permissions have the following effect:</p>
<ol>
<li>Read permission allows the directory contents to be listed</li>
<li>Write permission allows files or directories to be created or deleted
        within the directory</li>
<li>Execute permission allows the directory contents to be accessed</li>
</ol>
<p>UNIX permissions closest to the user take precedence. User permissions
      take priority over group permissions for the user who owns a file or
      directory, and both user and group permissions take priority over the
      'other' permissions for the user and group who owns the file.</p>
<h2>3. Setting UNIX File Permissions</h2>
<p>There are three utilities used for managing UNIX permissions:</p>
<ol>
<li><span style="font-family: monospace;">'ls -l'</span> &#8212; Displays a
        detailed list of directory contents</li>
<li><span style="font-family: monospace;">'chmod'</span> &#8212; Changes
        permissions</li>
<li><span style="font-family: monospace;">'chgrp'</span> &#8212; Changes group
        owners</li>
</ol>
<h3>3.1. <span style="font-family: monospace;">'ls -l'</span></h3>
<p>The ls commands outputs a list of files and directories within a given
      directory, and the <span style="font-family: monospace;">'-l'</span>
      option provides a detailed list of the directory contents (including
      permissions).<br/>
      <br/>
      Fig. 2 shows the output of <span style="font-family: monospace;">'ls -l'</span>
      on a test file, with each section labeled. The portions of the output
      related to UNIX file system permissions are colored red:</p>
<img alt="Permissions" height="205" src="../bin/img/perms1.png" width="743"/><br/>
    <span style="font-style: italic;"> Fig. 2: Labeled output of <span style="font-family: monospace;">'ls
        -l'</span></span><br/>
<p>The output of Fig. 2 tells us the following:</p>
<ol>
<li>The <span style="font-family: monospace;">'-'</span> file type
        indicates that this is a regular file</li>
<li><span style="font-family: monospace;">'rwx'</span> permissions for
        user indicates that the user who owns this file has read, write, and
        execute permissions for the file</li>
<li><span style="font-family: monospace;">'r-x'</span> permissions for
        group indicates that the group who owns this file has read and execute
        permissions for the file</li>
<li><span style="font-family: monospace;">'---'</span> permissions for
        'other' (i.e., everyone else) means that everyone other than the user or
        group who owns the file has no access to the file.</li>
<li>The file is owned by user 'walbert' and group 'support'</li>
</ol>
<p>&#160;</p>
<h3>3.2. <span style="font-family: monospace;">'chmod'</span></h3>
<p>The <span style="font-family: monospace;">chmod</span> command changes
      the permission on a given file or directory.<br/>
      <br/>
      <span style="font-family: monospace;">chmod</span> sets permissions in two
      ways.</p>
<ol>
<li>Using symbols</li>
<li>Using octal values</li>
</ol>
<p>This guide uses primarily octal values because they can be entered
      quickly. For more information on using symbols, refer to the <span style="font-family: monospace;">chmod</span>
      man page.<br/>
      <br/>
      When using octal values, a series of three digits defines the permissions
      for user, group, and other in that order. For example, a three-digit octal
      value of 640 would translate to read and write permissions for the user,
      read permission for the group, and no permission for others. When using
      octal values to define permissions, the new octal value entered will
      replace the existing permissions on the file or directory.<br/>
      <br/>
      (There is an optional leading digit which sets the setuid, setgid, and
      sticky bits. Usage of these permissions bits are outside the scope of this
      guide.)<br/>
      <br/>
      The following examples show common uses of <span style="font-family: monospace;">chmod</span>.
      After the <span style="font-family: monospace;">chmod</span> command, the
      output of the <span style="font-family: monospace;">ls -l</span> command
      is shown to demonstrate how the file permissions should look. Refer to the
      table in Fig. 1 for explanations of the octal values.</p>
<p><strong>3.2.1. Access for Only Yourself</strong></p>
<p><span style="font-family: monospace;">$ chmod 600 test_file<br/>
        $ ls -l test_file<br/>
        -rw------- 1 walbert ugrad 0 Nov&#160; 5 15:58 test_file</span></p>
<p><strong>3.2.2. Write Access for Yourself and Read Access for Your Group</strong></p>
<p><span style="font-family: monospace;">$ chmod 640 test_file<br/>
        $ ls -l test_file<br/>
        -rw-r----- 1 walbert ugrad 0 Nov&#160; 5 15:58 test_file</span></p>
<p><strong>3.2.3. Write Access for Yourself and Read Access for Everyone Else</strong></p>
<p><span style="font-family: monospace;">$ chmod 644 test_file<br/>
        $ ls -l test_file<br/>
        -rw-r--r-- 1 walbert ugrad 0 Nov&#160; 5 15:58 test_file</span></p>
<p><strong>3.2.4. Write and Execute Access for Yourself Only</strong></p>
<p><span style="font-family: monospace;">$ chmod 700 test_file<br/>
        $ ls -l test_file<br/>
        -rwx------ 1 walbert ugrad 0 Nov&#160; 5 15:58 test_file</span></p>
<p><strong>3.2.5. Write and Execute Access for Yourself, and Execute Access for
      Everyone</strong></p>
<p><span style="font-family: monospace;">$ chmod 755 test_file<br/>
        $ ls -l test_file<br/>
        -rwxr-xr-x 1 walbert ugrad 0 Nov&#160; 5 15:58 test_file</span></p>
<p><strong>3.2.6. Write Access to a Directory for Yourself, and Read Access for
      Your Group</strong></p>
<p><span style="font-family: monospace;">$ chmod 750 test_directory<br/>
        $ ls -l -d test_directory<br/>
        drwxr-x--- 2 walbert ugrad 2.0K Nov&#160; 5 15:59 test_directory/</span></p>
<p><strong>3.2.7. Remove All Non-User Access to a Directory and its Sub-directories</strong></p>
<p><span style="font-family: monospace;">$ chmod -R go-rwx test_directory<br/>
        $ ls -l -R test_directory<br/>
        test_directory/:<br/>
        total 4.0K<br/>
        drwx------ 3 walbert ugrad 2.0K Nov&#160; 5 16:53 foo/<br/>
        -rw------- 1 walbert ugrad&#160;&#160;&#160; 0 Nov&#160; 5 16:20
        test_file<br/>
        <br/>
        test_directory/foo:<br/>
        total 4.0K<br/>
        drwx------ 3 walbert ugrad 2.0K Nov&#160; 5 16:53 bar/<br/>
        -rw------- 1 walbert ugrad&#160;&#160;&#160; 0 Nov&#160; 5 16:53 test1<br/>
        <br/>
        test_directory/foo/bar:<br/>
        total 4.0K<br/>
        drwx------ 2 walbert ugrad 2.0K Nov&#160; 5 16:53 baz/<br/>
        -rw------- 1 walbert ugrad&#160;&#160;&#160; 0 Nov&#160; 5 16:53 test2<br/>
        <br/>
        test_directory/foo/bar/baz:<br/>
        total 0<br/>
        -rw------- 1 walbert ugrad 0 Nov&#160; 5 16:53 test3<br/>
      </span><br/>
      In this case, symbolic permission assignment was used, as octal permission
      assignments usually do not produce the intended result when applied
      recursively. The symbolic permissions used in this example, 'go-rwx', can
      be translated as &#8220;for group and other&#8212;remove read, write, and execute
      permissions.&#8221;</p>
<h3>3.3. <span style="font-family: monospace;">'chgrp'</span></h3>
<p>The <span style="font-family: monospace;">chgrp</span> command changes
      the group ownership on a given file or directory.<br/>
      <br/>
      Changing group ownership may be necessary when sharing files and
      directories with other members of your research group.</p>
<p><strong>3.3.1. Changing the Group Ownership of a File</strong></p>
<p><span style="font-family: monospace;">$ chgrp support test_file<br/>
        $ ls -l test_file<br/>
        -rw-rw---- 1 walbert support 0 Nov&#160; 5 15:58 test_file</span></p>
<p><strong>3.3.2. Changing the Group Ownership of a Directory and its Contents</strong></p>
<p><span style="font-family: monospace;">$ chgrp -R support test_directory<br/>
        $ ls -l -R test_directory<br/>
        test_directory:<br/>
        total 4.0K<br/>
        drwx------ 3 walbert support 2.0K Nov&#160; 5 16:53 foo/<br/>
        -rw------- 1 walbert support&#160;&#160;&#160; 0 Nov&#160; 5 16:20
        test_file<br/>
        <br/>
        test_directory/foo:<br/>
        total 4.0K<br/>
        drwx------ 3 walbert support 2.0K Nov&#160; 5 16:53 bar/<br/>
        -rw------- 1 walbert support&#160;&#160;&#160; 0 Nov&#160; 5 16:53 test1<br/>
        <br/>
        test_directory/foo/bar:<br/>
        total 4.0K<br/>
        drwx------ 2 walbert support 2.0K Nov&#160; 5 16:53 baz/<br/>
        -rw------- 1 walbert support&#160;&#160;&#160; 0 Nov&#160; 5 16:53 test2<br/>
        <br/>
        test_directory/foo/bar/baz:<br/>
        total 0<br/>
        -rw------- 1 walbert support 0 Nov&#160; 5 16:53 test3</span></p>
<h2>4. Finding Files with Unwanted Permissions</h2>
<p>This section provides information on the use of the find command to find
      and display files and directories with permissions that may be
      undesirable. All examples in this section will output results in the
      format typically provided by <span style="font-family: monospace;">ls -l</span>.<br/>
      <br/>
      All examples will use the following format:<br/>
      <br/>
      <span style="font-family: monospace;">find /dir/name -perm /005 -type f
        -print0 | xargs -0 ls -l</span><br/>
      <br/>
      Explanation of these options:<br/>
      <span style="font-family: monospace;">/dir/name</span> &#8212; Directory being
      searched<br/>
      <span style="font-family: monospace;">-perm</span> &#8212; Instructs <span style="font-family: monospace;">find</span>
      to match files based on given permissions<br/>
      <span style="font-family: monospace;">-type f</span> &#8212; Instructs <span style="font-family: monospace;">find</span>
      to match only regular files<br/>
      <span style="font-family: monospace;">-print0</span> &#8212; Sends output to
      stdout using the null character<br/>
      <span style="font-family: monospace;">xargs</span> &#8212; Command that builds
      another command to execute based on the content of stdin<br/>
      <span style="font-family: monospace;">-0</span> &#8212; Use the null character
      as the item delimiter</p>
<h3>4.1. Find All Files Accessible by 'Other' in Any Way</h3>
<p><span style="font-family: monospace;">$ find &#126; -perm /007 -type f -print0
        | xargs -0 ls -l<br/>
      </span><br/>
      This will display all files within your home directory that have read,
      write, or execute permissions for 'other.' This is useful for getting a
      general idea of what others can access in your home directory.</p>
<h3>4.2. Find All Files Readable by 'Other'</h3>
<p><span style="font-family: monospace;">$ find . -perm /004 -type f -print0
        | xargs -0 ls -l</span><br/>
      <br/>
      This will display all files within the current working directory that are
      world-readable. Many of these files will be 'common' files (such as shared
      libraries, icons, etc.) located in hidden directories, which are generally
      harmless. Any private files that appear on this list (e.g., private SSH
      keys or files on non-hidden directories) should be investigated.</p>
<h3>4.3. Find All Files Writable by 'Other'</h3>
<p><span style="font-family: monospace;">$ find /extra/research0 -perm /002
        -type f -print0 | xargs -0 ls -l</span><br/>
      <br/>
      This will display all files in the /extra/research0 directory that are
      writable by 'other.' Any files that appear in this list should be
      investigated, and in most cases, permissions should be changed to
      something more restrictive.</p>
<h3>4.4. Find All Files Executable by User or Group, and Writable by 'Other'</h3>
<p><span style="font-family: monospace;">$ find &#126; -perm -102 -type f -print0
        | xargs -0 ls -l&#160; # User<br/>
        $ find &#126; -perm -012 -type f -print0 | xargs -0 ls -l&#160; # Group</span><br/>
      <br/>
      This will display all files in your home directory that are both
      world-writable and executable by either the user or the group that owns
      the file. Files that are user-executable and world-writable are a major
      security vulnerability and should be fixed immediately unless you know
      exactly what you are doing.</p>
<h3>4.5. Find All Files Owned by a Specific Group</h3>
<p><span style="font-family: monospace;">$ find &#126; -group NameOfGroup -type f
        -print0 | xargs -0 ls -l</span><br/>
      <br/>
      This will display all files in your home directory that are owned by the
      specified group.</p>
<h3>4.6. Find All Files Not Owned by a Specific Group</h3>
<p><span style="font-family: monospace;">$ find . -not -group NameOfGroup
        -type f -print0 | xargs -0 ls -l</span><br/>
      <br/>
      This will display all files in the current working directory that are not
      owned by the specified group.</p>
