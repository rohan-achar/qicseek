<div id="content_title">Inappropriate Linux Configurations</div>

<blockquote>
<div id="content_text">

<p>In many cases, vendor-supplied files have inappropriate configurations
              which
should be changed to prevent others from obtaining unwanted access.
          </p>
<ul>
<li> <code>/etc/hosts.equiv</code> and <code>$HOME/.rhosts</code>
 
<ul>
<li> Decide if the file <code>/etc/hosts.equiv</code> is 
really
                required on your system.&#160; If you are running commands 
such
                as <code>rsh</code>, <code>rlogin</code>, etc., this 
file allows
                other hosts to be trusted by your system.&#160; Rlogin will 
then
                allow someone with the same account name to login to 
your machine
                from a listed host without supplying a password. This is
 a way
                that hackers can easily leap-frog from machine to 
machine. </li>
<li> Verify that the permissions on these files are set to 600.</li>
<li> Verify that the owner of <code>$HOME/.rhosts</code>&#160; 
is
                the same as the account's owner and that the owner of <code>/etc/hosts.equiv</code>
 is <i>root</i>.</li>
<li> Make sure that the file does NOT contain the symbol <b>"+"</b>
 on
                any line as this allows anyone access to this account or
 system.</li>
<li> Verify that the usage of netgroups within <code>.rhosts</code>
 does
                not allow unintended access to this account. Make sure 
that only
                the accounts and hosts you want to provide access to 
have access.</li>
<li> Do not use <b>'!'</b> or <b>'#'</b> in these files.&#160; 
There
                are no comment characters for these files. </li>
<li> Only trust hosts which are within your domain or under your direct managment. </li>
<li> Only use fully qualified hostnames (i.e., <i>hostname.ics.uci.edu</i>).</li>
</ul>
</li>
<p>&#160;</p>
<li> <code>/usr/lib/X11/xdm/Xsession</code> </li>
<p> Check this file for an <code>xhost</code> command with a
 <b>'+'</b>.&#160; Remove
              that line, since it allows anyone on the network (or 
possibly on
              the Internet) to talk to your X server, insert commands 
into windows, and read your console keystrokes.            </p>
<p>&#160;</p>
<li> <code>/etc/ttys</code> and <code>/etc/ttytab</code></li>
<p> The only terminal that should be set to <i>secure</i> 
should
              be the console.            </p>
<p>&#160;</p>
<li> <code>/etc/aliases</code> (or <code>/usr/lib/aliases</code>)</li>
<p> Check this mail alias file for inappropriate entries. When shipped, some alias
              files include an alias named <i>uudecode</i> or
 just <i>decode</i>.&#160; This
              can almost always be commented out.            </p>
<p>&#160;</p>
<li> System files
<ul>
<li> Check the permissions and ownership of system files &#160;and directories,
                especially the <b>/</b> (root) and <code>/etc</code> 
directories,
                and all system and network configuration files. </li>
<li> Examine file and directory protections before and 
after installing
                software. These procedures can cause file and directory 
protections
                to change without you being aware of it. </li>
</ul>
</li>
<p>&#160;</p>
<li> Setuid shell scripts are always potential security 
problems and can not be made secure. Do not create or allow setuid 
shell
              scripts, especially the <i>setuid root </i>ones<i><i>. </i></i></li>
</ul>


</div>
</blockquote>
