<p><h4>FRIENDS OF THE BREN SCHOOL</h4></p>
<br />
<p>As public funding for our state&rsquo;s universities declines and the costs and risks of corporate research and
  development escalate, there is increasing motivation for forging bonds between academia and industry. </p>
<p> The Bren School is research oriented - even at the undergraduate level - with formal projects addressing 
  hardware, software, algorithm design, artificial intelligence and the societal impacts of computing. </p>
<p> Research orientated education means Bren School students arrive at companies already comfortable on the 
  leading edge and poised to make an immediate impact.  In addition to students, our world renowed faculty 
  work with outside companies and frequently collaborate with professionals in other academic areas to create 
  even greater synergy. </p>
<p>This results in a combination of corporate insight, faculty guidance and student energy 
  that has proven time and again to be the spark that ignites tomorrow&rsquo;s products and services. </p>
<p>In 2006-07, the Bren School development office raised over $1 million in individual and corporate donations 
  (in addition to the $13 million in contract and grant funds collected by faculty, students and project 
  scientists). </p>
<p><a href="/about/annualreport/2006-07/donors.php">The Honor Roll of Donors</a> lists all of those individuals and corporations who generously donated to the Bren School in 2006-07. </p>
<p>The Bren School programs listed below offers individual and corporate donors an opportunity to interface 
  with our students and faculty. </p>
<p>
<blockquote>

<strong><a href="/community/friends/proj125/index.php">PROJECT ICS</a></strong><br />
  Through this in-class internship program, your company can augment its IT staff for a 10-week period partnering with a dynamic, senior-level team of Bren School students to start, develop or complete an existing technology project. </p>
<p><strong><a href="/community/events/extreme/index.php">hITEC</a></strong><br />
  The ICS Technology Entrepreneurship Compettion (hITEC) is a student competition designed to encourage the development of new products by UCI undergraduate and graduate students. hITEC, formerly <em>Extreme Computing</em>, offers you a chance to mentor a team of students as they develop a new product and attempt to enter it into the marketplace. </p>
<p><strong><a href="/community/scholarships/scholar_corporate.php">Scholarships and Fellowships</a></strong><br />
  Support a student by sponsoring a scholarship or fellowship. In forging relationships with the students they support, current donors have discovered that Bren School students are more than scholars, they also are community volunteers, dedicated youth leaders, responsible young adults and promising future professionals.</p>
</blockquote>
