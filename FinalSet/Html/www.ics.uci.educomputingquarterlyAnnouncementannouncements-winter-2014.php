<h3><a name="GITLAB"></a><a name="Gitlab_Beta_Testing"></a>Gitlab
Beta Testing</h3>
ICS Computing Support is running a Gitlab beta test site.
&#160;Research groups interested in having a managed Gitlab
site are encouraged to contact&#160;&lt;<a href="mailto:helpdesk@ics.uci.edu" style="color: #1155cc;" target="_blank">helpdesk@ics.uci.edu</a>&gt;.
<br/>
<br/>
Gitlab is a web interface to Git, a version control management
application similar to SVN.<br/>
<br/>
- Users can login using their ICS user
name/password<br/>
- Accounts may be requested for non-ICS users<br/>
- Users may start projects and add other ICS users to them as needed<br/>
<br/>
For more information on Gitlab, please visit<a href="https://gitlab.com/gitlab-org/gitlab-ce/blob/master/README.md">
