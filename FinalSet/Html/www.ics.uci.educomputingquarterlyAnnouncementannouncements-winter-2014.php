<h3><a name="POWER"></a><a name="Campus_Wide_Electrical_Power_Outage_"></a>Campus
Wide
Electrical Power Outage &amp; Central Plant Shutdown</h3>
We would like to remind everybody that Facilities Management has
scheduled two campus wide electrical power outage in February.
<ul>
<li>Saturday, Feb. 8, 5 a.m.:
Transfer to generator power, 30-minute outage</li>
<li>Monday, Feb. 10, 5 a.m.:
Transfer back to normal power, 30-minute outage</li>
</ul>
ICS Computing support plans to power down any&#160; non-essential
computing
in the data center that is not on UPS and backup generator.
&#160;This will
include most compute clusters and some research storage (/extra
storage). &#160; Systems will
be powered down beginning on Friday, Feb 7 at noon. &#160;Non
critical systems will be brought back up on Monday morning, Feb 10th.<br/>
<br/>
Critical services and services that have UPS and backup generator will
remain available during these power outages and throughout the weekend.
&#160;Services that will remain available during the weekend
include&#160;most ICS storage (home directories), web service,
database and&#160; mail service.<br/>
<br/>
Additional information is available at:<span style="text-decoration: underline;"> &#160;</span><a href="http://snap.uci.edu/viewXmlFile.jsp?cmsUri=public/FacilitiesManagement/General/CampusWidePowerOutage.xml">http://snap.uci.edu/viewXmlFile.jsp?cmsUri=public/FacilitiesManagement/General/CampusWidePowerOutage.xml</a><br/>
