<h3><a name="STORAGE"></a>Storage
and Backups</h3>
Researchers
and Computing Support added over 100TB of spinning disk this summer.
&#160;Like most storage, this space&#160;is network-accessible
from any ICS-supported host,
comes
with user-accessible snapshots, and is backed up to tape.<br/>
<br/>
If you
are a researcher
interested in purchasing a portion of this space, in terabyte
increments, or storage hardware, please contact helpdesk@ics.uci.edu for more information on this and
Dell discounts.<br/>
<br/>
Computing Support has also expanded backup capacity to keep up with
growing
storage demand. &#160;We now run two tape libraries and multiple disk-to-disk
backup pools. &#160;Quarterly backups will be
performed at the end of each quarter and stored for two years.<br/>
<br/>
<h3>Additional
notes
about ICS Backups</h3>
<span style="text-decoration: underline;">Snapshot
Backups</span><br/>
<br/>
<div style="margin-left: 40px;">File
system snapshots
are provided on most NFS-mounted directories (i.e. /home and /extra) as a user-accessible backup. &#160;
Snapshotted
directories offer two to four weeks of snapshot backups. Contact
<a href="mailto:helpdesk@ics.uci.edu">helpdesk</a>&#160;to find out the specifics on your file system.<br/>
<br/>
File
system snapshots
are a copy of a file system at a specific point in
time, even while the original file system continues to be updated and
used normally. &#160;File system snapshots are typically available
to
the directory owner.<br/>
<br/>
Information
on
accessing home directory snapshots is available at&#160;<a href="http://www.ics.uci.edu/computing/services/snapshot.php">http://www.ics.uci.edu/computing/services/snapshot.php</a></div>
<a href="http://www.ics.uci.edu/computing/services/snapshot.php"></a><br/>
<span style="text-decoration: underline;">Tape
Backups</span><br/>
<br/>
<div style="margin-left: 40px;">Tape
backups of /home
and /extra directories are made regularly. &#160;Tape backups are
intended for disaster recovery after critical hardware&#160;failure.
&#160;Please
keep in mind that directories named "scratch" are not backed up (case
insensitive).</div>
<br/>
<span style="text-decoration: underline;">Local
File Systems</span><br/>
<br/>
<div style="margin-left: 40px;">The
local file system
is NOT part of regular backups. &#160;This space is
volatile,&#160;impermanent,
and users should perform
their own backups of any information saved on a local file system.<br/>
<br/>
Data
may be stored on
the local file system in the /scratch and /tmp directories. &#160;Files are
deleted from the /tmp directory at boot, after they have not been
accessed for 10 days, or during routine system administration.<br/>
<br/>
Files
from /scratch
will be left intact except when the file systems are reformatted during
installation, hardware failure, or emergency system administration.</div>
