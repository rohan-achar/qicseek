<h3><a name="EMAIL"></a>Changes
to your ICS Email
Account&#160;</h3>
There are two significant changes coming to
the ICS email services.
&#160;First
and
foremost,&#160;ICS Google Apps for Education and Gmail accounts are
available for all ICS&#160;faculty, staff, and grads.
&#160;Computing
Support will be emphasizing ICS Gmail service in favor of the internal
ICS IMAP email system.<br/>
<br/>
ICS email users will also be able to opt
out of virus and spam email scanning. &#160;If you&#160;setup an ICS
Google
Apps account or otherwise forward email to an address outside of ICS, then your email should be scanned by your mail provider
instead of ICS mail scanners. &#160;(Note: &#160;Users
forwarding via procmail will still be subject to
ICS email virus and spam scanning).<br/>
