<div id="content_title">UCI Team Places Third in Microsoft’s Speller Challenge</div>

<blockquote>
<div id="content_text">

<p>
<img alt="Microsoft VP Harry Shum presents award certificate to UCI team leader Yasser Ganjisaffar" src="../../../bin/img/photos/MicrosoftSpellerChallenge_ceremony.jpg" width="400"/>
<br/><i><strong>Ph.D. student Yasser Ganjisaffar accepts the UCI team's award certificate from&#160;<i><strong>Microsoft VP Harry Shum</strong></i></strong></i></p>
<hr/>
<p>
After typing &#8220;compter science&#8221; into a search engine, most of us don&#8217;t think twice about getting results instead for the corrected term &#8220;computer science.&#8221; The mechanisms behind that correction, however, are anything but simple.
</p>
<p>
A team of UCI Bren School students recently delved into this problem by entering Microsoft Research and Bing&#8217;s &#8220;<a href="http://web-ngram.research.microsoft.com/spellerchallenge/" title="Microsoft Speller Challenge">Speller Challenge</a>&#8221; and finishing in third place among 100+ competitors worldwide. The aim of the Challenge was to build the best speller &#8212; one that proposes the most plausible spelling alternatives for each (potentially misspelled) search query. Spellers were encouraged to use cloud computing to process the terabytes of data needed for a Web-scale speller. 
</p>
<p>
Advised by Associate Professor Chen Li, the UCI team included Ph.D. students Yasser Ganjisaffar (team leader), Sara Javanmardi and Inci Cetindil; M.S. students Manik Sikka and Sandeep Paul Katumalla; visiting student Andrea Zilio from Italy; and volunteer student Narges Khatib-Astaneh, who will join the M.S. program in ICS this fall. They were recognized during an award ceremony held July 20 at the Microsoft Bing Headquarters in Bellevue, Wash. 
</p>
<p>
&#8220;Unlike previous data mining competitions in which we had participated, this challenge needed several months of work just to prepare the data sets and the infrastructure needed for processing queries,&#8221; says Ganjisaffar, who with Javanmardi works under the supervision of Associate Professor Cristina Lopes. &#8220;It was a great experience to solve a Web-scale problem.&#8221;
</p>
<p>
The UCI team&#8217;s program, <a href="http://flamingo.ics.uci.edu/spellchecker/#qSpell">qSpell</a>, addresses the challenges of Web-scale natural language processing. According to the students&#8217; analysis, roughly 10-15 percent of the queries sent to search engines contain spelling errors or typos. These typos must be corrected for users to find their desired results. 
</p>
<p>
&#8220;My research group has been working on topics related to Web search, especially on how to deal with errors and inconsistencies in data and queries,&#8221; Li explains. &#8220;This Microsoft Challenge provided us a great opportunity to test our techniques and help students solve interesting Web-scale problems. The students worked very hard, and I am proud of what they have achieved.&#8221; 
</p>
<p style="float: right; margin-top: 0em; margin-bottom: 2em;">&#8212; PG </p>


