<p><h4>ABOUT STUDENT AFFAIRS</h4></p>
<br />
<img src="/bin/img/photos/people/sao_staff.jpg" alt="photo: student affairs office staff" height="154" width="670">
<p>The Student Affairs Office (SAO) is staffed by nine professionals and several peer advisors who are committed to the success of our students, guiding them through important milestones and encouraging their timely progress to graduation. In a typical year, SAO has about 3,200 advising contacts with undergraduates and about 1,200 with graduate students. </p>
<p>Additionally, SAO hosts numerous special events for prospective and current students, fosters student leadership and campus involvement, and encourages community among School faculty and student scholars. These efforts contribute to the School&rsquo;s goals of attracting, retaining and graduating students whose accomplishments while at UCI - and later - reflect the quality and scope of their Bren School of ICS education.</p>
