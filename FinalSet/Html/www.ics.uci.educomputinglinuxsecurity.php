<div id="content_text">

<p>With the power and flexibility of UNIX and UNIX variants like Linux
come many potential security holes.&#160; To limit the vulnerability of your
workstation as well as that of the system, please read the following 
suggestions
and apply the patches appropriate to your particular version
of Linux.</p>
<ul>
<li> <a href="#advice">General
 Advice</a></li>
<li> <a href="services.php">
 Unnecessary Services</a></li>
<li> <a href="access.php"> 
Allowing Access to Your Workstation</a></li>
<li> <a href="tools.php">
 Useful Tools</a></li>
<li><a href="file-security.php">Understanding and Setting File Permission</a></li>
</ul>
<hr width="100%"/>
<h3><a name="advice"></a>General Advice</h3>
<ul>
<li> Use a current version of your version of Linux. For 
example,
              Red Hat 5.1 is known to be particularly favored by 
hackers. </li>
<li> Apply security 
 patches  as soon
              as they become available. As soon as a vulnerability 
becomes known,
              attack kits are posted on the Internet to take advantage 
of it. </li>
<li> Turn off <a href="services.php">
 services</a> you do not need.
              Most versions of Linux come with preconfigured options 
enabled
              for things that most people never use. Many of these are 
popular
              targets for attack. </li>
<li> Look into available <a href="tools.php">
 tools</a> to
              prevent break-ins to your workstation.</li>
<li> &#65279;&#65279;&#65279;&#65279;Keep track of your logs for repeated access attempt or other unusual activity. These log files are usually located in the <code>/var/log</code> directory.</li>
</ul>


