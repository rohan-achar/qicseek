<div id="content_title">Mapping Home/Group Directory - Windows</div>

<blockquote>
<div id="content_text">

<span style="color: #990000;">
<p style="text-align: center;"><strong>*** Important ***</strong></p>
<p style="text-align: center;"><strong><em>If you are using the UCInet Mobile Access (UCI wireless) or connecting from off-campus (including on-campus housing),</em></strong></p>
<p style="text-align: center;"><strong><em> please make sure that you are connected via the <a href="http://www.oit.uci.edu/security/vpn/vpninstall.html" title="VPN">UCI VPN</a> first.</em></strong></p>
</span>
<p>&#160;</p>
<p><i>* The instructions below are for Windows 7 operating systems. If you are using Windows XP, the settings are the same, but the name of those settings will differ slightly (e.g. My Computer instead of Computer).</i></p>
<ul>
<li><a href="#quick">Quick instructions</a></li>
<li><a href="#pictures">Instructions with pictures</a></li>
<li><a href="#errors">Common&#160;errors</a></li>
</ul>
<p>&#160;</p>
<hr/>
<p><a name="quick"></a><strong>Quick Instructions</strong></p>
<ol>
<li>If you are on the campus wireless network or at an off-campus location (which includes on-campus housing), you need to connect to the campus <strong><a href="http://www.oit.uci.edu/security/vpn/vpninstall.html" title="VPN">VPN</a>&#160;</strong>first. &#160;Please install the VPN client, as WebVPN will not work for the purposes of this discussion. &#160;Once you have connected to the VPN using your UCI credentials, you may continue.</li>
<li>Right click on <b>Computer</b> and select <b>Map Network Drive...</b>
</li>
<li>Fill out the <b>Drive</b> and <b>Folder</b> information.
<ul>
<li><b>Drive:</b>&#160;Choose any drive letter that is available (you can choose H: to match the setup on the ICS machines if preferred).</li>
<li><b>Folder:</b>&#160;Fill in the path of where your home directory or group account is located.
<ul>
<li>Home directory: <code>\\tron.ics.uci.edu\your_username</code>
<ul>
<li>If you are running Windows 8, please enter: <code>\\samba.ics.uci.edu\your_username</code></li>
</ul>
</li>
<li>Group directory: <code>\\samba.ics.uci.edu\group_acct_name</code></li>
</ul>
</li>
<li>Please keep in mind that the server name may be different for 
                some users. If you cannot map with the above server 
names, please 
                contact <a href="mailto:helpdesk@ics.uci.edu">helpdesk</a>
 to 
                find out which server you should be mapping to.</li>
<li>If you would like to always have your network drive available, 
                check the box next to <b>Reconnect at Logon</b>. This 
will cause 
                the network connection to be re-established at every 
login so 
                you do not have to repeat this process every time. <i>*** You will still need to establish a connection to the VPN each time.</i></li>
</ul>
</li>
<li>If you are connecting from a machine which is not connected to the ICS domain (e.g. a laptop using wireless or your home desktop machine), please place a checkmark on&#160;<b>Connect using a different credentials</b>.
<br/>
<br/>
<ul>
<li><b>Username:</b>&#160;your_username</li>
<li><b>Password:</b>&#160;your_ics_password</li>
</ul>
</li>
<p style="padding-left: 60px;"><i>* If Domain does not say UCI-ICS or ics.uci.edu, please enter your username in the form UCI-ICS\your_username</i><i><br/></i></p>
<p style="padding-left: 60px;"><i>** For XP users: Please enter your username in the form UCI-ICS\your_username</i><i><br/></i></p>
<li>Click <strong>OK </strong>and then <strong>Finish</strong>. You are now done.
</li>
</ol>
<p>&#160;</p>
<hr/>
<p><strong><a name="pictures"></a>Instructions with pictures</strong></p>
<ol>
<li>If you are on the campus wireless network or at an off-campus location (which includes on-campus housing), you need to connect to the campus&#160;<strong><a href="http://www.oit.uci.edu/security/vpn/vpninstall.html" title="VPN">VPN</a>&#160;</strong>first. &#160;Please install the VPN&#160;client, as WebVPN will not work for the purposes of this discussion. &#160;Once you have connected to the VPN using your UCI credentials, you may continue.</li>
<li>Right click on&#160;<b>Computer</b>&#160;and select&#160;<b>Map Network Drive...</b><i><br/></i></li>
<li>Fill out the&#160;<b>Drive</b>&#160;and&#160;<b>Folder</b>&#160;information.

<ul>
<li><b>Drive:</b>&#160;Choose any drive letter that is available (you can choose H: to match the setup on the ICS machines if preferred).</li>
<li><b>Folder:</b>&#160;Fill in the path of where your home directory or group account is located.
<ul>
<li>Home directory:&#160;<code>\\tron.ics.uci.edu\your_username</code></li>
<li>Group directory:&#160;<code>\\samba.ics.uci.edu\group_acct_name</code></li>
</ul>
</li>
<li>Please keep in mind that the server name may be different for some users. If you cannot map with the above server names, please contact&#160;<a href="mailto:helpdesk@ics.uci.edu">helpdesk</a>&#160;to find out which server you should be mapping to.</li>
<li>If you would like to always have your network drive available, check the box next to&#160;<b>Reconnect at Logon</b>. This will cause the network connection to be re-established at every login so you do not have to repeat this process everytime.&#160;<i>*** You will still need to establish a connection to the VPN each time.</i>
<br/>
<br/>
<img alt="Map drive" height="460" src="../bin/img/account/mapdrive_win/path.jpg" width="628"/></li>
<br/>
</ul>
</li>
<li>If you are connecting from a machine which is not connected to the ICS domain (e.g. a laptop using wireless or your home desktop machine), please place a checkmark on <b>Connect using a different credentials</b>.
<ul>
<li><b>Username:</b> your_username</li>
<li><b>Password:</b> your_ics_password
<p><i>* If Domain does not say UCI-ICS or ics.uci.edu, please enter Username in the form UCI-ICS\your_username</i><i><br/></i></p>
<p><i>** For XP users: Please enter Username in the form UCI-ICS\your_username</i><i><br/></i></p>
<p><img alt="connect as" height="276" src="../bin/img/account/mapdrive_win/username.jpg" width="439"/></p>
</li>
</ul>
</li>
<li>Click <b>OK </b>and then <b>Finish</b>. You are now done.</li>
</ol>
<p>&#160;</p>
<hr/>
<p><strong><a name="errors"></a>Common Errors</strong></p>
<ul>
<li>If you are on the UCI Mobile Access or are connecting from off-campus locations, please make sure that you are connected to the <a href="http://www.oit.uci.edu/security/vpn/vpninstall.html">UCI VPN</a> before trying to map the network drive.</li>
<li>Please make sure that you are entering in your ICS password and NOT your UCInetID password.</li>
<li>If you get an error about the path not found, please try replacing the server name with its corresponding IP address:
<ul>
<li>samba.ics.uci.edu - 128.195.1.18</li>
<li>tron.ics.uci.edu - 128.195.1.5</li>
</ul>
</li>
</ul>


