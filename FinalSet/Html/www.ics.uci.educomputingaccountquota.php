<div id="content_title">Account Quota</div>

<blockquote>
<div id="content_text">

<p>Each account type is assigned a different amount of quota as listed below. If your advisor has his/her own space, you may request for your account to be moved there. &#160;This may result in a higher quota. &#160;Please check with your advisor for details.</p>
<p>If you need more space, please try using the Student Extra Space (not backed up). &#160;To access this storage, ssh into openlab.ics.uci.edu, navigate to&#160;/extra/ugrad_space, and create a folder with your username, for example: /extra/ugrad_space/peter.&#160; Please be aware that anything that you store here will not be backed up, so plan accordingly. &#160;</p>
<p><a href="#allocation">Quota Allocation</a></p>
<p><a href="#check">Checking Your Quota</a></p>
<p><a href="#reminder">Quota Reminder</a></p>
<hr/>
<p><a name="allocation"></a><b>Quota Allocation</b></p>
<ul>
<li>Students
<ul>
<li>Undergrad - 500MB</li>
<li>Grad - 10GB
<ul>
</ul>
</li>
</ul>
</li>
</ul>
<ul>
<li>Staff <br/>
<ul>
<li>8GB</li>
</ul>
</li>
</ul>
<ul>
<li>Faculty <br/>
<ul>
<li>10GB</li>
</ul>
</li>
</ul>
<ul>
<li>Visitor <br/>
<ul>
<li>300MB</li>
</ul>
</li>
</ul>
<p>&#160;</p>
<p><a name="check"></a><b>Checking Your Quota</b></p>
<ul>
<li>Login to one of the <a href="../linux/hosts.php">ICS hosts</a></li>
<li>At the prompt, type in the command <code>quota -v</code></li>
<li>You will then get a return of something like this:
<ul>
<li> An account that is under quota.
<pre><code>suzieanteater% /usr/sbin/quota -v<br/>Disk quotas for santeater (uid 1234):<br/>Filesystem       usage  quota  limit timeleft  files  quota  limit timeleft<br/>/home/santeater   1409   2000   4000            209      0      0<br/></code></pre>
In this example, Suzie Anteater has a quota of 2 MB, and a hard limit of
4 MB. She has used 1.4 MB worth of space and has 209 files in her 
account.
</li>
<li> An account that is over quota.
<pre><code>peteranteater% /usr/sbin/quota -v<br/>Disk quotas for panteater (uid 4567):<br/>Filesystem       usage  quota  limit  timeleft  files  quota  limit timeleft<br/>/home/panteater   2404  4000   4000  3.0 days          209      0      0<br/></code></pre>
In this example, Peter's usage is above the quota limit, and unless
his quota is brought down below the limit within 3 days, he will be
locked out of his account, or he may lose files.
</li>
</ul>
</li>
</ul>
<p>&#160;</p>
<p><a name="reminder"></a><b>Quota Reminder</b></p>
<p>Would you like to receive a reminder when you are almost maxing out your quota? Please login to <a href="../email/mailboss.php">Mailboss</a> and choose when to receive such notifications.</p>


