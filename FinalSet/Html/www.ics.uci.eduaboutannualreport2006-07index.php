Dear Bren School Community,
<p>  2006-07 was an exciting academic year at UC Irvine&rsquo;s Donald Bren School of
  Information and Computer Sciences.  </p>
<p>We had many successes and firsts as we
  moved into the new Donald Bren Hall and welcomed many new graduate and 
  undergraduate students. </p>
<p>Today, we are an academic community of more than 1,500 students, over 100 full-time faculty and staff, and approximately 7,000 alumni worldwide.  </p>
<p>In 
  teaching and scholarship, we continue to be among the top in information and computer sciences.  </p>
<p>To add to our list of accolades, our Networked Systems <br />
  program was just rated number one by Academic Analytics, in addition to being ranked third in Information Systems. </p>
<p> At the Bren School, we have a unique perspective on computing and 
  information technology, stimulating society daily.  </p>
<p>Our vibrant community, comprised of researchers and educators as well as industry-leading scholars, 
  explores innovative topics ranging from building complete computer systems on 
  chips smaller than a finger nail to developing user interface systems that allow 
  engineers on opposite sides of the world to collaborate effectively.</p>
<p> I invite you stay in touch with us throughout the year by <a href="http://www.ics.uci.edu/community/news/rss/news.xml">subscribing to our RSS 
  feed</a> or visiting our <a href="http://www.ics.uci.edu">Web site</a> regularly.</p>
<p> Many thanks for your continued support of our vision.</p>
<p><img src="/bin/img/signature/djrsig.gif" alt="debra j. richardson's signature" width="136" height="62" /></p>
<p>Debra J. Richardson
  <br />
  The Ted and Janice Smith Family Foundation Dean </p>
