<h3>COMPUTER SCIENCE &amp; ENGINEERING 181: SENIOR DESIGN PROJECT (TWO-QUARTER)</h3>
<!-- -->
<!-- -->
<p class="image_with_wrapped_text">
<img alt="miHOME" class="right" src="../../../bin/img/photos/spotlight/student_project_showcase_2012/MiHome.jpg"/>
