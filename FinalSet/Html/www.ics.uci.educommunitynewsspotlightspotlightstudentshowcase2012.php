<div class="course_section">
<h3>INFORMATICS 191: SENIOR DESIGN PROJECT (THREE-QUARTER)</h3>
<!-- -->
<!-- -->
<p class="image_with_wrapped_text">
<img alt="CASETRAK" class="right" src="../../../bin/img/photos/spotlight/student_project_showcase_2012/CaseTrak.jpg"/>
</p>
<p><b>&#8220;CASETRAK&#8221;</b></p>
<p>
CaseTrak is a mobile application for medical students and residents to optimize the workflow of capturing statistical data related to clinical cases in which they are involved.
</p>
<p><b>Team Memory Lane:</b> Melody Budiono, Julie Darwish, Simon Huynh, Marcel Pufal, Kevin Sar
</p>
<p><b>Sponsor:</b> Department of Clinical Informatics, Medical Center, UCI
</p>
<!-- -->
<!-- -->
<p class="image_with_wrapped_text">
<img alt="FIELD STUDY WEB PORTAL" class="right" src="../../../bin/img/photos/spotlight/student_project_showcase_2012/FieldStudyPortal.jpg"/>
</p>
<p><b>&#8220;FIELD STUDY WEB PORTAL&#8221;</b></p>
<p>
The Field Study Web Portal is an overhaul of the current Social Ecology field study program website. This new web portal improves usability and communication with features such as the progress bar and web forms, and reduces student time spent searching for a field site with a searchable, sortable and filterable web table of field site listings. Students also have the ability to view the status of their applications, while community partners may review and give feedback to student applications within the web portal.
</p>
<p><b>Team Omega:</b> Grant Arjani, Chris Lang, Martina Mickos
</p>
<p><b>Sponsor:</b> Ashley Vikander, School of Social Ecology, UCI
</p>
<!-- -->
<!-- -->
<p class="image_with_wrapped_text">
<img alt="INDYAVIDS MOBILE" class="right" src="../../../bin/img/photos/spotlight/student_project_showcase_2012/IndyaVids.jpg"/>
</p>
<p><b>&#8220;INDYAVIDS MOBILE&#8221;</b></p>
<p>
IndyaVids Mobile is an Android application for the website IndyaVids, an instructional video site for Indian culture. The application allows users to search, explore and watch videos. As well you can register as a member and upload your own videos with a smartphone.
</p>
<p><b>Team Dynamic in Motion:</b> Matthew Ito, Brandon Lovrein, Zafar Mehboob, Tri Pham, William Yeh
</p>
<p><b>Sponsor:</b> Ash Kumra, IndyaVids
</p>
<!-- -->
<!-- -->
<p class="image_with_wrapped_text">
<img alt="PORTFELO" class="right" src="../../../bin/img/photos/spotlight/student_project_showcase_2012/Portfelo.jpg"/>
</p>
<p><b>&#8220;PORTFELO&#8221;</b></p>
<p>
Creating a new experience for photographers, models, make-up artists, wardrobe stylists and hair stylists to market their portfolio and network with each other.
</p>
<p><b>Team Aperture:</b> Danielle Yu, Evan White, Ferdinand Lucero Jr., Jordan White, Michael Cupino
</p>
<!-- -->
<!-- -->
<p class="image_with_wrapped_text">
<img alt="PRESIDIO MOBILE" class="right" src="../../../bin/img/photos/spotlight/student_project_showcase_2012/PresidioMobile.jpg"/>
</p>
<p><b>&#8220;PRESIDIO MOBILE&#8221;</b></p>
<p>
The Presidio Trust is an organization that maintains and preserves the Presidio National Park in San Francisco. This involves recording information about plant life, such as a species&#8217; abundance or transferring less common species away from construction zones. Currently, a Presidio Trust field operative records plant information by walking around the park and taking notes using pen and paper. Afterwards, he returns to his office and types his notes into a desktop system in order to update the Presidio Trust plant records. Presidio Mobile, an Android application, streamlines the recording process by allowing plant information to be recorded and sent straight to the records without having to leave the park. In addition, Presidio Mobile can search through the records and view plant locations on a map.
</p>
<p><b>Team Chromatic:</b> Kenny Compass, Josh Ettlin, Linh Lam, Steven Nguyen, Sanjay Yashpaul
</p>
<p><b>Sponsor:</b> The Presidio Trust of San Francisco
</p>
<!-- -->
<!-- -->
<p class="image_with_wrapped_text">
<img alt="READ &amp; MORE" class="right" src="../../../bin/img/photos/spotlight/student_project_showcase_2012/ReadAndMore.jpg"/>
</p>
<p><b>&#8220;READ &amp; MORE&#8221;</b></p>
<p>
Reading is commonly taught through phonics. However, children with Down Syndrome are visual learners; they learn to read more efficiently by actually seeing the connections between text and pictures. Read &amp; More is an interactive learning application designed with visual learners in mind. The app transforms the physical material used in classrooms into a virtual learning tool that can be used in classrooms and at home.
</p>
<p><b>Team LearnEd:</b> Candace Chen, Thanh Le, Muhammad Zaman, Matan Lurey, Vatsal Shah
</p>
<p><b>Sponsor:</b> Dana Halle, Down Syndrome Foundation of Orange County
<!-- -->
<!-- -->
</p>
<p class="image_with_wrapped_text">
<img alt="THE STARLING QUARTERLY" class="right" src="../../../bin/img/photos/spotlight/student_project_showcase_2012/StarlingQuarterly.jpg"/>
</p>
<p><b>&#8220;THE STARLING QUARTERLY&#8221;</b></p>
<p>
We&#8217;ve created an interactive writing environment where students may learn and explore writing through poetry. The process is feedback intensive with a strong focus on enhancement through revision. We&#8217;ve looked to the community as a source of support and encouragement for the students, enabling them to view published works within the Starling Quarterly &#8212; a publicly accessible literary journal.
</p>
<p><b>Team Scribble:</b> Minda Wang, Jonathan Chang, Mike Tang, Christine Liang, Kevin Huynh
</p>
<p><b>Sponsor:</b> Sue Cronmiller, UCI Poetry Exchange
<!-- -->
<!-- -->
</p>
<p class="image_with_wrapped_text">
<img alt="WHAT&#8217;S UP, JAMES?" class="right" src="../../../bin/img/photos/spotlight/student_project_showcase_2012/WhatsUpJames.jpg"/>
</p>
<p><b>&#8220;WHAT&#8217;S UP, JAMES?&#8221;</b></p>
<p>
What&#8217;s Up, James?&#8221; is an interactive comic book style iPad application. The purpose of this application is to facilitate and enforce correct English pronunciation by delivering it with a fun and interactive user interface. It also includes voice read-aloud capabilities.
</p>
<p><b>Team Applied Minds:</b> Stephan Chilingaryan, Ammar Takieldin, Shawn Ridgeway, Warren Trinh
</p>
<p><b>Sponsor:</b> Noriyasu Suzuki
<!-- -->
<!-- -->
</p>
