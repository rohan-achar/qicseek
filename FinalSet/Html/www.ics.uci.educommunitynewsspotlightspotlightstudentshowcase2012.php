<h3>INFORMATICS 117: SOFTWARE SYSTEM DESIGN PROJECT (ONE-QUARTER)</h3>
<!-- -->
<!-- -->
<p class="image_with_wrapped_text">
<img alt="GSA CLOUD RESOURCE MANAGEMENT INTERFACE" class="right" src="../../../bin/img/photos/spotlight/student_project_showcase_2012/GSAcloud.jpg"/>
</p>
<p><b>&#8220;GSA CLOUD RESOURCE MANAGEMENT INTERFACE&#8221;</b></p>
<p>
The goal of the project is to take all the videos stored on GSA&#8217;s local hard drives and move them into Amazon&#8217;s S3 (Simple Storage Service) and keep track of each video&#8217;s meta data along with a link to the video resource in Amazon&#8217;s SDB (Simple Database). We have created a web app that will allow GSA to effectively manage their videos and database records by searching through the database using established keywords, inserting video meta data to match database structure, and uploading videos in bulk through a direct link to Amazon&#8217;s Management Console.
</p>
<p><b>Team Green:</b> Chris Gau, Humza Bhakhrani, Jonathan Yao, Kaveh Vazirzadeh, Zaid Ibrahim
</p>
<p><b>Sponsor:</b> GreenScreen Animals (GSA)
<!-- -->
<!-- -->
</p>
<p class="image_with_wrapped_text">
<img alt="THE HARVEST CLUB WEB-BASED DATABASE SYSTEM" class="right" src="../../../bin/img/photos/spotlight/student_project_showcase_2012/HarvestClub.jpg"/>
</p>
<p><b>&#8220;THE HARVEST CLUB WEB-BASED DATABASE SYSTEM&#8221;</b></p>
<p>
The OC Food Access Coalition runs a program called The Harvest Club. The Harvest Club wants build capacity to expand what they do in the community. They are currently using various database applications to manage all their information. The goal of this project is to merge all the existing software technologies into a single system and simplify the daily activities of The Harvest Club volunteers and staff.
</p>
<p><b>Team Nyan Catz:</b> Duy Thuy, Du The Du, Steven Salat, Lawrence Neill, Fernando Vargas
</p>
<p><b>Sponsor:</b> OC Food Access Coalition
<!-- -->
<!-- -->
</p>
<p class="image_with_wrapped_text">
<img alt="ICS.JOBS" class="right" src="../../../bin/img/photos/spotlight/student_project_showcase_2012/ICSjobs.jpg"/>
</p>
<p><b>&#8220;ICS.JOBS&#8221;</b></p>
<p>
ICS.jobs is a website that connects students in the Donald Bren School of Information and Computer Sciences with employers. It enables students to post profiles (including resumes) and employers to post jobs. Among several features, students may search posted jobs, and employers may search posted profiles.
</p>
<p><b>Team ICS.jobs:</b> Steven Neisius, James Supancic III, Makoto Kadota, Uyen Nguyen, Peter Park
</p>
<p><b>Sponsors:</b> Dan Frost and Colleen O&#8217;Higgins, Bren School, UCI
<!-- -->
<!-- -->
</p>
<p class="image_with_wrapped_text">
<img alt="SCAPES (STUDYING COLLABORATIVE AUTHORING PRACTICES IN EDUCATIONAL SETTINGS" class="right" src="../../../bin/img/photos/spotlight/student_project_showcase_2012/SCAPES.jpg"/>
</p>
<p><b>&#8220;SCAPES (STUDYING COLLABORATIVE AUTHORING PRACTICES IN EDUCATIONAL SETTINGS&#8221;</b></p>
<p>
SCAPES analyzes the history of revisions of Google Docs. It aims to help research in patterns of collaborative writing and research in teacher-student interactions through Google Docs.
</p>
<p><b>Team Sucy:</b> Jordan Speer, Grace Uchida, Michael Cupino, Danielle Yu
</p>
<p><b>Sponsor:</b> Judith Olson, Bren School, UCI
<!-- -->
<!-- -->
</p>
