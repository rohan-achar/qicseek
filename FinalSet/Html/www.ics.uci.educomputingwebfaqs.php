<hr/>
<ol>
<li><b><a id="mypage" name="mypage"> Can I have a webpage on this server?</a></b>
<p> If you have an ICS UNIX account, you can create your own web page. &#160;See <a href="personalpage.php"> Creating Your Own Web Space 
              </a> for more information. </p>
</li>
<li><b><a id="coolpage" name="coolpage"> Will you help me set up a cool 
            webpage?</a></b>
<p> No. &#160;If this is for a class, contact your instructor or TA. &#160;Otherwise, 
              there many good HTML resources online. &#160;Use your favorite search engine to search for information on WWW programming.
            </p>
</li>
<li><b><a id="link" name="link">Will you make a link to my webpage?</a></b>
<p> 
              <!--
If you are an undergraduate student, the program which links
undergraduate home pages to the directory runs only once a week, so
please do not ask us to add your page to the listing. It is done
automatically.
<P>
-->
              If you are a graduate student or visiting researcher or faculty, 
              please send an email message to <a href="mailto:webmaster@ics.uci.edu"> webmaster@ics.uci.edu.</a>&#160;&#160;<!--
If you are an undergraduate student we apologize.  Presently this feature is broken.  
Once we make some repairs to the departmental accounts database we plan to reinstitute it.  Sorry about the inconvenience.
<p>
-->We do not link home pages to servers outside the campus. &#160;Please 
              do not waste your time and our time by making such a request. </p>
</li>
<li><b><a id="counter" name="counter"> How do I add a counter to my 
            home page?</a></b>
<p> We do not support the usage of counters. &#160;They are very wasteful 
              of bandwidth and system resources. &#160;We appreciate your consideration 
              on this matter. </p>
</li>
<li><b><a id="cgi" name="cgi"> How do I run <i>cgi-bin</i> programs? 
            </a></b>
<p> We do not permit users to have or use <i>cgi-bin</i> scripts other 
              than those already on the server: <code>calendar</code>, <code>date</code>, 
              <code>finger</code>, <code>fortune</code>, <code>imagemap</code> 
              and <code>uptime</code>. </p>
</li>
<li><b><a id="static" name="static"></a> <b>What sort of webpages 
            are allowed on this server?</b></b>
<p>Only static html webpages are allowed on the main webserver. &#160;PHP webpages are 
              not allowed.</p>
</li>
<li><b><a id="students" name="students"></a>Is there a server where 
            I can run PHP and cgi scripts?</b>
<p>You may login to the UNIX server <i>students.ics.uci.edu</i> and create 
              a webspace for yourself. &#160;Login and create a public_html folder. &#160; If you want to run cgi scripts, create a .htaccess file inside the public_html folder with the following options:</p>
<p>
Options -Indexes +ExecCGI<br/>
AddHandler cgi-script .cgi .pl .py</p>
<p>The URL would be http://students.ics.uci.edu/&#126;<i>loginname</i>. &#160;For security reasons these systems are stand-alone and do not mount 
              any NFS based directories, such as /home or /extra. &#160;You will 
              need to SSH and download applications directly to the system or 
              use SCP software like WinSCP. &#160;The servers are not backed up, so you 
              need to do it manually. &#160;A great tool for doing such would be using the <em>rsync </em>command. &#160;You will be responsible for maintaining the security of your website. &#160;If you download an application, make sure you check for updates regularly&#160;or sign up for the application's 
              mailing list.</p>
</li>
<li><b><a id="quota" name="quota"> How does this affect my quota? </a></b>
<p> Since your personal web space is contained in your home directory, 
              it is included in your quota. &#160;For more information on quotas, see 
              <a href="../account/quota.php"> ICS Instructional Quotas.</a></p>
</li>
