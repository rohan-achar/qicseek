<div id="content_title">Account Renewal</div>

<blockquote>
<div id="content_text">

<p><b>Student</b></p>
<ul>
<li>Major
<ul>
<li>Your ICS account will be automatically renewed as long as you remain under a major in the Bren School of ICS.</li>
</ul>
</li>
<li>Non-major
<ul>
<li>Your account is only active for the quarter in which you are enrolled in a course under the Bren School of ICS. The account will automatically renew the next time you are enrolled in an appropriate course. If it has been two years since you have had an active account, you will have to create the account again. </li>
</ul>
</li>
<li>Extension
<ul>
<li>Please bring in a receipt to CS 346L as proof&#160;that you have paid for your course/s. The lab manager will then extend your account for the quarter. You will have to do this at the beginning of each quarter.</li>
</ul>
</li>
</ul>
<p><b>Staff</b></p>
<ul>
<li>Your account will be renewed by your supervisor annually.</li>
</ul>
<p><b>Faculty</b></p>
<ul>
<li>Your account will be automatically renewed annually.</li>
<li>You may extend accounts for your students/visitors annually.
<ul>
<li>Login to <a href="https://elnino.ics.uci.edu/">el nino</a> system.</li>
<li>Select <b>Extend My Users</b> from the toolbar on the left.</li>
<li>If an account is not listed or if you are no longer the advisor of a listed account, please contact <a href="mailto:helpdesk@ics.uci.edu">helpdesk</a>.</li>
</ul>
</li>
</ul>


</div>
</blockquote>
