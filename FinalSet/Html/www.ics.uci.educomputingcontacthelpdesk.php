<h3>How does the ICS Helpdesk work?</h3>
The ICS Helpdesk uses a program called Request Tracker (RT). &#160;The RT system assigns
a unique number to each ticket, which helps keep track of each help request. &#160;Critical to the success 
of
the Helpdesk, every request must be input to RT to be assigned a ticket 
number,
whether the request originates as an e-mail message, phone message,
or walk-in visit. &#160;A general outline of the process follows.<br/>
<br/>
<ul>
<li> <b>E-MAIL MESSAGE</b><br/>
      
<ul>
<li>E-mail messages sent to helpdesk@ics.uci.edu are automatically input 
to RT
          and assigned a ticket number (#3 for purposes of this 
explanation).
        </li>
<li>The Helpdesk analyst on duty will triage the ticket and
 assign it
          to the appropriate technician.
        </li>
<li>Once the ticket is assigned to a technician, the sender
 of the original
          request will be notified of the ticket number by e-mail.
        </li>
<li>Adding new information to the ticket is as simple as 
sending another
          e-mail to helpdesk@ics.uci.edu with this contained in the 
subject line
          of the message: <code>[ics.uci.edu #3]</code> <br/>
        Please include the square brackets [ ] or simply reply to the original message leaving the subject line intact.
        </li>
<li>If the technician cannot resolve the request within one
 working day,
          the requestor will be notified of the estimated time of 
completion
          and an brief explanation of the delay.
        </li>
<li>Once the request is resolved, the ticket will be closed
 and the requestor
          will be notified by e-mail.
        </li>
<li>If the ticket is not resolved to the requestor's 
satisfaction, the
          ticket can re-opened by responding to the closed ticket 
message and
          the ticket, with its original number, will be re-entered into 
RTs 'open'
          queue.
      </li>
</ul>
</li>
<li> <b>TELEPHONE AND WALK IN</b><br/>
      
<ul>
<li>Helpdesk analysts will be responsible for creating tickets within
          RT for all telephoned and walk-in requests.
        </li>
<li>Once the ticket is created by the Helpdesk analyst, the
 process follows
          the same path as outlined above.
      </li>
</ul>
</li>
</ul>
<p>&#160;</p>
<h3>What about e-mail messages sent to support@ics.uci.edu?</h3>
Messages sent to support@ics.uci.edu are still monitored. &#160;If 
appropriate,
messages to 'support' will be forwarded to Helpdesk and assigned a 
ticket
number.
<p>&#160;</p>
