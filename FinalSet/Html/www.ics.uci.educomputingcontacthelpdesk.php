<h3>What is the ICS Helpdesk?</h3>
The Helpdesk is a service of the ICS Computing Support Group. The purpose of the Helpdesk is to provide a single point&#160;of contact&#160;and record for technical support requests from ICS faculty, staff, and students.
<p>&#160;</p>
<h3>Why a Helpdesk?</h3>
<ul>
<li>To improve the communications between the people who need services and
    the people who provide services.
  </li>
<li>To organize service requests to ensure prompt and complete 
service.
  </li>
<li>To simplify service request history through the database 
underneath RT.
  </li>
<li>To increase services by allowing other groups within the 
School to use
    RT.
</li>
</ul>
<p>&#160;</p>
