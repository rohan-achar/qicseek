<div id="content_title">Sophos Anti-Virus on Mac</div>

<blockquote>
<div id="content_text">

<p>* Before installing Sophos, please make sure that you do not have any other anti-virus programs already installed.</p>
<p>** If you already have Sophos installed and has problem updating, please uninstall it following the instructions <a href="http://www.sophos.com/en-us/support/knowledgebase/14179.aspx">here</a>. Then do a re-install using the instructions below.</p>
<p>*** If you are 
connecting from off campus or if you are on the UCI wireless 
network, please make sure that you are connected via the <a href="http://www.oit.uci.edu/security/vpn.html">UCI VPN</a> first 
before proceeding.</p>
<ol>
<li>From the <b>Finder</b> menu, click on <b>Go</b> and 
select <b>Connect to Server</b>.</li>
<li>For the server address, enter:
<p><code>smb://sophos-server.ics.uci.edu/sophos_mac</code></p>
</li>
<li>Enter your ICS username and password.</li>
<li>Navigate to the folder that matches your OS X install, and double click on the <strong>.dmg</strong>&#160;file to mount the drive.</li>
<li>Once the drive is mounted, double click on the <b>Sophos Anti-Virus.pkg</b> file to start the install.</li>
<li>Once the installation completes, click on the Sophos shield on the 
top 
right corner and select <b>Open Sophos Anti-Virus Preferences</b>.</li>
<li>Click on <b>Auto Update</b>, then click on the lock in the 
lower left corner to allow the following changes to be made.
<ul>
<li><b>Update from Primary Location: </b>Network Volume</li>
<li><b>Address:</b>
<code>smb://sophos-server.ics.uci.edu/SophosUpdate/CIDs/S000/ESCOSX</code>
		      <br/><i>*WARNING: The address is case-sensitive; S000 is the 
capital S follow by 3 zeroes.</i></li>
<li><b>Username:</b> your ICS username</li>
<li><b>Password:</b> your ICS password</li>
</ul>
</li>
<li>Click on the lock to prevent further changes and close the window.</li>
<li>Click on the Sophos shield up at the top again and select <b>Update Now</b>.</li>
<li>Updates need to be performed manually. &#160;It is recommended
            that you do this weekly to obtain the latest virus 
definitions. &#160;Sophos
            has a new version released each month so, once a month, the 
update
            will take longer as a new version needs to be downloaded and
 installed.
          <br/><i>*** If you are 
connecting from off campus or if you are on the UCI wireless 
network, you will need to be connected via the VPN in order to do the 
update.</i></li>
</ol>


</div>
