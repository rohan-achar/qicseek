<p><a name="what is"></a><strong>What is an ICS account?</strong></p>
<p>Your ICS account is used to access ICS resources, such as the computers in the ICS labs, your ICS email account, the computers on the UCI-ICS domain, and any ICS servers. Your ICS account is distinct and separate from your <a href="https://www.oit.uci.edu/ucinetid/">UCInetID</a>.</p>
<p><a name="who"></a>Who can get an account?</p>
<ul>
<li>Students (Undergrads &amp; Grads): must be under one of the majors under the Bren School of ICS or be enrolled in a course under the Bren School of ICS. Courses under another school but equivalent to one under our school also qualify.</li>
<li>If you are a non-ICS student/faculty working with a professor under our school, you may also qualify for an account.</li>
<li>Any staff or faculty members who are under Bren School of ICS.</li>
</ul>
<p><a name="how"></a><strong>How do I get an ICS account?</strong></p>
<ul>
<li>Students (Undergrads &amp; Grads) - Please bring your student ID card and see the lab attendant in CS 364.</li>
<li>Extension students - Please bring your student ID card and proof of enrollment to the lab manager in CS 346L.</li>
<li>Staff/visitor/faculty - Please have your department personnel send in an account request to helpdesk.</li>
</ul>
<p><a name="enrolled"></a><strong>I am enrolled in an ICS course, why can't I get an account?</strong></p>
<p>Our system is linked to the UCI registrar's 
database, and we receive updates once a
          	day.  If your classes were dropped because you have not paid 
your fees, you will not be eligible for an account
          	until your fees are paid.  If you are adding a class, please wait overnight to ensure that we get the updated 
information.
          	</p>
<p><a name="where"></a><strong>Where do I go to log in?</strong></p>
<p>Students taking ICS classes may access the ICS labs located on the
            1st and 3rd floors of the Computer Science building.  Check 
the <a href="http://www.ics.uci.edu/%7Elab">Lab Information</a> page for information on lab locations, 
operating hours, and hardware and software specifications.
            Note that these labs are not "public" labs in the sense that
 they are <b>only</b> for students taking ICS
            courses.</p>
<p><a name="off-campus"></a><strong>How do I access my account from off-campus?</strong></p>
<ul>
<li><a href="mapdrive_win.php">Windows</a></li>
<li><a href="mapdrive_mac.php">Mac</a></li>
<li><a href="../linux/hosts.php">Unix/Linux</a></li>
</ul>
<p><b><a name="policy"></a>Now that I have an ICS account, what are my 
responsibilities as a fellow user of a multiuser system?</b></p>
<p>All UCI and ICS students are expected to follow a
 system of ethical academic and computing
          	policies designated by the University of California and the 
Donald Bren School of ICS.  You will be required to read
          	these policies as part of your account activation process, and
 you are responsible for knowing what is and is not
          	acceptable behavior regarding your ICS account and the use of
 school equipment and facilities.
          	
</p>
<p>For details, please refer to the <a href="../policy/index.php">Computing Policy</a> section.</p>
<b><a name="cannot login"></a>I entered my username and password, but the system 
won't log me on.  What's wrong?</b>
<p>If this is the very first time you have tried logging on:
				</p>
<ol type="a">
<li>Make sure you have activated your account.  If not, check out 
the				<a href="activate.php">Account
 Activation</a> page.</li>
<li>Wait at least 2 hours after activating before you attempt to 
log on.&#160;</li>
</ol>
<p> ...if you still can't log on, check out the tips below.</p>
<p>If you have logged on successfully during this quarter:
				</p>
<ol type="a">
<li>Make sure you typed your username correctly.</li>
<li>Make sure you typed your password correctly.</li>
<li>Make sure you have selected "UCI-ICS" as the log on domain (UCI-ICS\username).</li>
</ol>
<p>	...if you still can't log on, contact the Lab Assistant in CS 
364.</p>


