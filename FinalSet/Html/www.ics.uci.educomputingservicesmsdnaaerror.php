<div id="content_title">MSDNAA Error Codes</div>

<blockquote>
<div id="content_text">

<p>The following are the most common error codes and possible solutions. &#160;If this page does not help you complete your
		download, visit CS 364 and ask a Lab Assistant for help.</p>
<p>SDC 00100</p>
<blockquote>Unable to find the source CD (expecting to find a SDC file on a local CD).</blockquote>
<p>SDC 00200</p>
<blockquote>A general error that is typically caused by:<br/>
		(a) insufficient disk space to unpack, or<br/>
		(b) a disk error</blockquote>
<p>SDC 00300</p>
<blockquote>
<p>Unable to read from media, which is typically caused by the PC being:<br/>
		(a) unable to connect to network resource (for a Network-Based install), or<br/>
		(b) unable to read from CD (for a secure CD-based install)</p>
<p>Please note that this is a network connection error only.</p>
</blockquote>
<p>SDC 00400</p>
<blockquote>Unable to complete the download, which is typically caused by:<br/>
		(a) an error in the configuration of the download package, usually due to a bad hosting location<br/>
		(b) no Internet connection found<br/>
		(c) Internet connection lost<br/>
		(d) a firewall or proxy server between the client station and hosting location, or<br/>
		(e) insufficient disk space to download</blockquote>
<p>SDC 00500</p>
<blockquote>CRC check failed, which can mean that the package is corrupt and unusable. This is typically caused by:<br/>
		(a) an incomplete transfer<br/>
		(b) a corruption during transfer, or<br/>
		(c) a corrupt package on the host server</blockquote>
<p>Error 70072</p>
<blockquote>Install limit has been reached, which is typically caused by:<br/>
		(a) the customer having attempted to download a product too many times to different folders (it is possible that no downloads have been successful), or<br/>
		(b) the customer having already successfully downloaded/installed the software, and now wanting to download again</blockquote>
<p>Error 70077</p>
<blockquote>Parameter authorization failure, which typically means that:<br/>
		(a) no hosting location has been provided on the system for the product, or<br/>
		(b) the delivery client is corrupt (delete and download again).</blockquote>


</div>
</blockquote>
