<div id="content_title">ICS Google Account FAQs</div>

<blockquote>
<div id="content_text">

<p><strong>Q. &#160;What is an ICS Google Apps Account?</strong></p>
<p>A. &#160;ICS&#160;<span>Google Apps&#160;</span>is an installation of&#160;<strong>Google Apps for Education</strong>, a package of online applications that makes communicating and collaborating at school easier and more efficient. The cornerstone of Google Apps is Gmail.</p>
<p><strong>Q. &#160;Will my ICS email address change?</strong></p>
<p>A. &#160;Your ICS email address will still be username@ics.uci.edu.</p>
<p><strong>Q. &#160;Will my personal information be shared with anyone outside of UC Irvine?</strong></p>
<p>A. &#160;<span>Your personal information will not be shared with anyone.</span></p>
<p><span><strong>Q.&#160; How do I setup my email client on my computer, phone or tablet?</strong></span></p>
<p><span></span>A. Use <a href="https://support.google.com/mail/troubleshooter/1668960?rd=1">Gmail's instructions</a>&#160;for setting up your email client (e.g. Thunderbird).</p>
<p><strong>Q. &#160;<span>Will my old email be transferred to my new ICS Google Apps account?</span></strong></p>
<p>A. &#160;Your old mail will not be transferred automatically and will remain on the ICS email server accessible via any IMAP client (including Gmail). There are no immediate plans to end ICS IMAP email service and email stored on the ICS IMAP servers will remain available.&#160;</p>
<p><strong>Q. &#160;How do I copy my old mail to my ICS Google Account?</strong></p>
<p>A. &#160;You can move your email from the ICS server to the ICS Google account manually.</p>
<ul>
<li>Set your email client to connect to the ICS IMAP service (i.e. Thunderbird) using the <a href="thunderbird_setup.php">ICS email setup instructions</a>.</li>
<li>Enable IMAP on your ICS Google Apps account and set same email client to connect to the ICS Google Apps IMAP server: &#160;<a href="https://support.google.com/mail/troubleshooter/1668960?rd=1">Gmail's instructions</a>.
<ul>
<li>Select <strong>I want to enable IMAP</strong>&#160;and follow the steps.</li>
<li>Choose&#160;<strong>Thunderbird </strong>as your client and follow the steps.</li>
<li>Select <strong>Ok, confirm my settings</strong> and use the given configuration.</li>
</ul>
</li>
<li>Email can be dragged and dropped between email accounts once your email client is setup.</li>
</ul>
<p><strong>Q. &#160;What if I had already signed up for a Google Account using my ICS email address?</strong></p>
<p>A. &#160;The next time you login to Google Apps with your ICS email address you will be instructed to merge accounts. Follow the directions. Send an email message to helpdesk@ics.uci.edu if you have any questions.</p>
<p><strong>Q. &#160;<span>What if I am already forwarding my UC Irvine email to a personal Gmail account?</span></strong></p>
<p>A. &#160;<span>ICS Google Apps is a separate service from a standard Gmail account. If you switch your forwarding address to your ICS Google Apps account, email messages addressed to username@ics.uci.edu will go to the ICS Google Apps account.</span></p>
<p><strong>Q. &#160;Where can I find more answers about my ICS Google Apps account?</strong></p>
<p>A. &#160;<span>Please visit the&#160;</span><a href="http://learn.googleapps.com/gmail">Gmail</a><span>&#160;page at the Google Apps Learning Center.</span></p>
<p><strong>Q. &#160;How do I reset my ICS Google Apps&#160;password?</strong></p>
<p>A. &#160;Like with any ICS account, contact support to have your password reset.</p>
<p><strong>Q. Can I sign on to multiple accounts in the same browser?</strong></p>
<p>A. <span>Gmail will allow you to have multiple accounts open. So you can read your personal Gmail account and the uci/ics account in the same browser.&#160; </span><br/><span>For more information see<span>&#160;</span></span><a class="urlextern" href="https://support.google.com/accounts/answer/1721977?hl=en" rel="nofollow" title="https://support.google.com/accounts/answer/1721977?hl=en">https://support.<span class="search_hit">google</span>.com/accounts/answer/1721977?hl=en</a></p>
<p><strong>Q. How do I enable a vacation message on my Google Account?</strong></p>
<p>A.<span>&#160;V</span>ia the Gmail web client. Click the Gear icon, then Settings. Choose the General tab and scroll down to the Vacation responder.</p>
<p>You can choose to turn on the Vacation responder for a specified date range. It will automatically turn off on the end date. It will allow you to set the subject and message. You can also set who will receive the vacation message, people in your contacts and/or people in the ICS Gmail domain.&#160;<a class="urlextern" href="https://support.google.com/mail/bin/answer.py?hl=en&amp;answer=25922" rel="nofollow" title="https://support.google.com/mail/bin/answer.py?hl=en&amp;answer=25922">View article in Help Center</a></p>
<p><strong>Q. What is the storage capacity for ICS Gmail?</strong></p>
<p>A. The storage capacity of a UCI Gmail account is currently 25GB.</p>
<p><strong>Q. Can I send and receive email from other accounts within my ICS Gmail account?</strong></p>
<p>A. Yes, to change the From: address of your messages, follow the instructions <a href="http://mail.google.com/support/bin/answer.py?hl=en&amp;answer=22370">here</a>.</p>
<p><strong>Q. How do I set up my signature?</strong></p>
<p>A. Login to UCI Gmail. Click on the Gear icon at the upper right of the page. Click Settings. Verify that you are in the General tab. Find the Signature section and enter your signature in the box. Now press Save Changes at the bottom of the page.</p>
<p>See also: &#160;<a href="http://www.google.uci.edu/faq.html">http://www.google.uci.edu/faq.html</a></p>


