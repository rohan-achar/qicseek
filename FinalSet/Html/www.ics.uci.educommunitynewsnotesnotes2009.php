<p><strong><a name="tsudik_ipsec2009"></a>Tsudik to give keynote on secure device pairing at IPSEC 2009</strong></p>
<div class="img_caption_left"> <img alt="photo: gene tsudik" height="90" src="../../../bin/img/photos/faculty/gtsudik.jpg" width="70"/>
<p><strong>Gene<br/>
                  Tsudik</strong></p>
</div>
<p>Professor of Computer Science <strong>Gene Tsudik</strong> will be giving an invited keynote talk entitled "Secure and Usable Device Pairing" at the 5th <a href="http://www.ispec2009.net/">Information Security Practice and Experience Conference (ISPEC 2009)</a> to be held April 13-15, 2009, in Xi&#8217;an, China.</p>
<p>&#8220;Secure Device Pairing&#8221; is the process of bootstrapping a secure channel between two or more previously unassociated personal devices over a (usually wireless) human-imperceptible communication channel. Lack of prior security context and absence of common trust infrastructure open the door for so-called "Man-in-the-Middle" (or "Evil Twin") attacks. Mitigation of these attacks requires user involvement in the device pairing process.</p>
<p>Tsudik&#8217;s talk will summarize notable secure device pairing techniques, comparing and contrasting their advantages, shortcomings and limitations, followed by the first comprehensive and comparative evaluation of these methods.</p>
<p>ISPEC is an annual conference that brings together researchers and practitioners to provide a confluence of new information security technologies, their applications and their integration with IT systems in various vertical sectors.  More information about ISPEC can be found at the Conference Web site:  http://www.ispec2009.net/.</p>
<p>Tsudik's research interests are mainly in computer/network security, privacy and applied cryptography. His recent work focuses on privacy in Internet services, RFID systems and mobile ad hoc networks, as well as security in sensor networks and storage systems.</p>
<p>His research also covers secure group communication, in particular, group key agreement, group signatures and group access control. He also is interested in database security and public key cryptography. </p>
