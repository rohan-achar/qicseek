             <p><strong><a name="fowlkes_ramanan_desai_marr_prize"></a>Fowlkes, Ramanan and Desai 
awarded
                Marr Prize in Kyoto, Japan</strong></p>
             <div class="img_caption_left">
                <img alt="photo: Charless Fowlkes" height="90" src="../../../bin/img/photos/faculty/fowlkes.jpg" width="70"/>
                <p><strong>Charless<br/>Fowlkes</strong></p>
                <br/>
                <img alt="photo: Deva Ramanan" height="90" src="../../../bin/img/photos/faculty/dramanan.jpg" width="70"/>
                <p><strong>Deva<br/>Ramanan</strong></p>
             </div>
             <p>A paper entitled "Discriminative models for multi-class 
object layout" by PhD student <strong>Chaitanya Desai</strong>
                and Assistant Professors <strong>Deva Ramanan</strong> 
and <strong>Charless Fowlkes</strong> received the Marr Prize at
                the International Conference on Computer Vision (ICCV) 
held the first week of October in Kyoto, Japan. </p>
             <p>The prize is awarded to the best paper at ICCV and is 
considered one of the top honors in computer vision.  The award is
                named after David Marr, a theoretical neuroscientist who
 made profound contributions to the theory of both human and machine
                vision in the 1970's. </p>
             <p>The paper describes research on a new approach to 
modeling contextual relations between objects in an image (e.g. bottles
                are often seen resting on top of tables but not the 
other way around). The system automatcially learns these relations from
                example images and uses this information to outperform 
existing approaches to object detection. </p>
             <p>Fowlkes' research is in computational vision, both in 
understanding the information processing capabilities of the human
                visual system and in developing machine vision systems. 
He is also interested in applying computer vision techniques to
                automating the analysis of biological data and 
developing algorithmic tools for understanding morphology and spatial 
aspects
                of gene expression. </p>
             <p>Ramanan's research interests span computer vision, 
machine learning, and computer graphics. His past work focused on the
                analysis of human movement from video, including 
tracking people and recognizing their actions.  Current interests 
include
                object recognition, large-scale image/video processing, 
structured-prediction approaches to learning, and activity recognition. </p>

