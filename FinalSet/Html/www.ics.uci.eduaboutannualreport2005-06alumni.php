<p><h4>NOTABLE ALUMNI 2005-06</h4></p>
<br />
<p><strong>Jim Berney '89</strong> <br />
  Sony Pictures Imageworks Visual Effects Supervisor Jim Berney was nominated for an Academy Award in 2006 for his achievement in visual effects. His nomination was for Buena Vista's <em>The Chronicles of Narnia: The Lion, the Witch and the Wardrobe</em>.</p>
<p><strong>Steven Joe '90</strong> <br />
  Steven Joe, President and CEO of D-Link North America, who was recognized by the editors of CMP Media&rsquo;s VARBusiness as one of North America&rsquo;s &quot;Top 50 Technology Innovators,&quot; was honored on April 27th at the UCI Alumni Association&rsquo;s Lauds &amp; Laurels Ceremony. UC Irvine Chancellor Michael V. Drake also presented Steven Joe with the &ldquo;Advocate of the Year&rdquo; award during the March 17 meeting of the UCI Chief Executive Roundtable.</p>
<p><strong>Paul Mockapetris, Ph.D. '81</strong> <br />
  Paul Mockapetris, chairman and chief scientist of Nominum Inc. in Redwood City, California was elected to the National Academy of Engineering for his contributions to the Internet, including pioneering and standardizing the Domain Name System (DNS).</p>
