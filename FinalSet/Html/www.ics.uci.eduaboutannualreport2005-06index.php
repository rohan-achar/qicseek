Dear Bren School Community,
<p>It is my pleasure to introduce the 2005-06 Donald Bren School of Information and Computer Sciences Annual Report. Through our annual report we hope to keep you &ndash; our close friends and colleagues &ndash; abreast of our accomplishments, as well as challenges.</p>
<p>Today, we are an academic community of more than 1,500 students, over 100 full-time faculty and staff, and approximately 6,500 alumni worldwide. In teaching and scholarship, we continue to be among the top in information and computer sciences. <em>The Chronicle of Higher Education</em> ranked us third in Information Sciences research. </p>
<p>I invite you stay in touch with us throughout the year by <a href="/community/news/rss/">subscribing to our RSS feed</a> or visiting our <a href="http://www.ics.uci.edu/">Web site</a> regularly.</p>
<p>Many thanks for your continued support of our vision as well as our research and education. </p>
<p><img src="/bin/img/signature/djrsig.gif" alt="debra j. richardson's signature" width="136" height="62" /></p>
<p>Debra J. Richardson
