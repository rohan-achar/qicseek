<h3><span style="font-size: 1.17em;">Creation</span></h3>
<p>An ICS Google Apps account will be automatically created for all ICS faculty, staff, and graduate students. &#160;</p>
<p>If you are a new user then you need take no additional action. &#160;Your ICS Google Apps account was setup, and your forwarding address was set. &#160;You may login <a href="http://mail.google.com/a/ics.uci.edu" title="ICS Google Aps Login">here</a>&#160;and access your email.</p>
<p>Please continue on to Setup if you have an existing ICS account.</p>
<h3>Setup</h3>
<p>If you are an existing ICS user, then you will need to login to&#160;<a href="https://elnino.ics.uci.edu" title="https://elnino.ics.uci.edu">https://elnino.ics.uci.edu</a>&#160;and make two additional changes.&#160; <strong>Note: </strong>from off-campus locations, you must use the <a href="http://www.nacs.uci.edu/security/vpn/" target="_blank" title="UCI VPN">UCI VPN</a>&#160;client.</p>
<p>Change your password by clicking on the "Change Password" button.&#160;</p>
<p><span>Under forwarding address, select "ICS Gmail". &#160; Leave the textbox empty as demonstrated below.</span></p>
