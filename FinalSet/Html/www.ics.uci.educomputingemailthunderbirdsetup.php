<div id="content_title">Thunderbird Setup</div>

<blockquote>
<div id="content_text">

<p><em>This tutorial is based on Thunderbird 24.1.0. If you're using a different version, the naming convention may differ, but the settings will remain the same.</em>
</p>
<ol>
<li>Start up Thunderbird for the first time (or go to Tools, Account Settings, Account Actions, Add Mail Account)</li>
<li>In the <b>Mail Account Setup</b> window, enter your name and email address. Enter your password here if you want to have it remembered. Click <strong>Continue</strong>.</li>
<li>Thunderbird will now attempt to look for the IMAP information. Please make sure that you have <strong>IMAP </strong>selected.</li>
<li>Click on <b>Manual config </b>in the lower left corner to make sure that the information is as follow:
<ul>
<li>Incoming
<ul>
<li>IMAP</li>
<li>Server hostname: <strong>imap.ics.uci.edu</strong></li>
<li>Port: <strong>993</strong></li>
<li>SSL: <strong>SSL/TLS</strong></li>
<li>Authentication: <strong>Normal password</strong></li>
</ul>
</li>
<li>Outgoing
<ul>
<li><a href="http://www.oit.uci.edu/email/tbird3-IMAP-pc.html">UCI SMTP</a>.&#160;If you're connecting from a laptop or from off campus, please use the UCI SMTP server settings or your ISP server settings. <br/>
<ul>
<li>For UCI SMTP, see settings from step number 7 in the link provided.</li>
</ul>
</li>
<li>ICS SMTP. If your machine is in on the UCI-ICS domain, please use the following settings.&#160;
<ul>
<li>
</li>
<li>Server hostname: <strong>smtp.ics.uci.edu</strong></li>
<li>Port: <strong>587</strong></li>
<li>SSL: <strong>STARTTLS</strong></li>
<li>Authentication: <strong>Normal password</strong></li>
<li>Username:<strong> &lt;your username&gt;</strong>
<ul>
</ul>
</li>
</ul>
</li>
</ul>
</li>
</ul>
<br/><img alt="advanced window" src="../bin/img/email/setup.jpg"/>
</li>
<li>Click <strong>Re-test</strong> and then <strong>Done</strong></li>
<li>If you <em>did not</em> choose to have your password saved, you will now be prompted to enter your email password.</li>
<li>Go to <strong>Tools </strong>and select <strong>Account Settings</strong></li>
<li>Under <strong>Server Settings</strong>, click on the <b>Advanced</b>&#160;button
<ul>
<li>Make sure that <b>IMAP server directory</b> is empty</li>
<li>Uncheck <b>Show only subscribed folders</b></li>
<li>Uncheck <b>Allow server to override these namespaces</b></li>
</ul>
<br/><img alt="advanced window" src="../bin/img/email/advanced.jpg"/>
</li>
<li>This last step is optional. If you have many email messages, you may want to disable Message Synchronizing so that your email loads more quickly.
<ul>
<li>While still on <strong>Account Settings</strong> window, select <strong>Synchronization &amp; Storage</strong></li>
<li>Uncheck <strong>Keep messages for this account on this computer</strong></li>
</ul>
</li>
</ol>


</div>
